                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a microbiology test detect a virus like the flu?</h4>
                                                    <p style="margin:0; font-size:15px;">Standard "culture" tests are
                                                        for bacteria and fungi. Viruses are much harder to grow. To
                                                        detect viruses like Flu, COVID-19, or HIV, we use specialized
                                                        tests like **PCR (Polymerase Chain Reaction)** which looks at
                                                        the virus's DNA/RNA instead of growing it.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What does "Sensitive" mean on my lab report?</h4>
                                                    <p style="margin:0; font-size:15px;">If a report says "Sensitive"
                                                        (S) next to an antibiotic, it means that specific drug is highly
                                                        effective at killing the bacteria in your body. If it says
                                                        "Resistant" (R), the drug will not work and should not be used.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Why do I need a "Mid-Stream" urine sample?</h4>
                                                    <p style="margin:0; font-size:15px;">The first bit of urine washes
                                                        away bacteria sitting on the skin. The "mid-stream" represents
                                                        what is actually happening inside your bladder. This prevents a
                                                        "false positive" result where we find skin bacteria instead of
                                                        infectious bacteria.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is a "Negative" culture?</h4>
                                                    <p style="margin:0; font-size:15px;">A negative culture means that
                                                        after 48 to 72 hours of incubation, no harmful bacteria grew.
                                                        This is good news! It means your symptoms may be caused by
                                                        something else, like a virus or inflammation, and you probably
                                                        don't need antibiotics.</p>
                                                </div>
