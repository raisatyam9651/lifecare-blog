                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can Type 2 diabetes be reversed?</h4>
                                                    <p style="margin:0; font-size:15px;">While "reversal" is a complex
                                                        term, many patients can achieve "remission"—where blood sugar
                                                        levels are normal without medication—through significant weight
                                                        loss and lifestyle changes.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Why do I need an eye exam every year?</h4>
                                                    <p style="margin:0; font-size:15px;">Diabetes can damage the small
                                                        blood vessels in the retina (diabetic retinopathy). Early
                                                        detection through a dilated eye exam can prevent permanent
                                                        vision loss.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is brown ugali better for diabetics than white ugali?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, brown ugali (made from
                                                        whole grains like sorghum or millet) has a lower glycemic index
                                                        and more fiber, which means it raises blood sugar more slowly
                                                        than white maize ugali.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is a "hypo" (hypoglycemia)?</h4>
                                                    <p style="margin:0; font-size:15px;">Hypoglycemia is when blood
                                                        sugar drops below 3.9 mmol/L. It can cause shakiness, sweating,
                                                        and confusion. It must be treated immediately with fast-acting
                                                        sugar.</p>
                                                </div>
