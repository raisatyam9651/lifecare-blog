                                                        Can x-rays cause cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">While high doses of radiation
                                                        are a risk factor, the dose from a digital dental X-ray is so
                                                        fundamentally low that it has never been medically linked to an
                                                        increased cancer risk.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Do children need X-rays too?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, because a child's mouth is
                                                        growing and changing rapidly. X-rays help prevent orthodontic
                                                        issues and detect "baby bottle" decay that can affect adult
                                                        teeth.</p>
