                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is the most common risk of surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">Minor risks include bruising at
                                                        the incision site or nausea from anesthesia. More serious but
                                                        rare risks include infection or blood clots. At LifeCare, we use
                                                        blood-thinning medications and compression stockings to prevent
                                                        clots during and after the procedure.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long should I fast before surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">Generally, we require patients
                                                        to have nothing to eat or drink (not even water) for **8 hours**
                                                        before surgery. This is for your safety, as it prevents stomach
                                                        contents from entering the lungs while you are under anesthesia.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I go home the same day?</h4>
                                                    <p style="margin:0; font-size:15px;">Many laparoscopic procedures
                                                        like gallbladder removal (cholecystectomy) or hernia repairs are
                                                        now "day surgeries," meaning you can go home once you are fully
                                                        awake and comfortable. For larger open surgeries, a stay of 2-3
                                                        days is usually required for safety.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I stop my smoker habit before surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Smoking significantly
                                                        reduces the oxygen in your blood and slows down the healing of
                                                        your wound. It also increases the risk of lung complications
                                                        during anesthesia. Stopping even just 2 weeks before surgery
                                                        dramatically improves your safety.</p>
                                                </div>
