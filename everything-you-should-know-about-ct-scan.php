                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a CT scan noisy?</h4>
                                                    <p style="margin:0; font-size:15px;">It makes a humming or whirring
                                                        sound as the internal parts spin, but it is much quieter than an
                                                        MRI. You won't need ear protection.</p>
