                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I get a CT scan if I'm pregnant?</h4>
                                                    <p style="margin:0; font-size:15px;">**Generally, no.** While the
                                                        risk is low, we avoid CT scans for pregnant women whenever
                                                        possible to protect the developing baby from radiation. We
                                                        usually suggest an Ultrasound or an MRI as safer alternatives.
                                                    </p>
