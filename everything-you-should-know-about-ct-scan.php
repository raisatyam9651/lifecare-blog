                                                        How soon will I get my results?</h4>
                                                    <p style="margin:0; font-size:15px;">In an emergency, results are
                                                        available in under an hour. For routine scans, the radiologist
                                                        usually takes a few hours to a day to carefully review the
                                                        thousands of images and write a detailed report.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a CT scan detect cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** It is one of the most
                                                        powerful tools for finding cancers in the lungs, liver,
                                                        pancreas, and kidneys. However, it cannot tell if a tumor is
                                                        definitely benign (non-cancerous) or malignant—a biopsy is
                                                        usually needed for that final confirmation.</p>
                                                </div>
