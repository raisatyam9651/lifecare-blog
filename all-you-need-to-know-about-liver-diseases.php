                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can the liver really regenerate?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes! The liver is unique. If
                                                        even 25% of a healthy liver remains, it can grow back to its
                                                        full size within weeks. However, advanced scarring (cirrhosis)
                                                        limits this ability.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is the difference between Hepatitis and Cirrhosis?</h4>
                                                    <p style="margin:0; font-size:15px;">Hepatitis is the inflammation
                                                        of the liver (often from a virus). Cirrhosis is the late stage
                                                        of scarring caused by long-term inflammation from various
                                                        causes.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is coconut oil good for the liver?</h4>
                                                    <p style="margin:0; font-size:15px;">While some studies suggest
                                                        benefits, coconut oil is high in saturated fat. Moderation is
                                                        key. A diet rich in unsaturated fats (like olive oil) is
                                                        generally better for liver health.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a "Liver Detox" supplement help?</h4>
                                                    <p style="margin:0; font-size:15px;">Be cautious. Many "detox"
                                                        products are unproven and some can actually cause liver injury.
                                                        The best way to detox is to drink water and eat a clean,
                                                        balanced diet.</p>
