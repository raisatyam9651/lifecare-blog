                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        I found a lump. What should I do now?</h4>
                                                    <p style="margin:0; font-size:15px;">**Don't panic, but don't
                                                        wait.** Most lumps are benign. Book an appointment with a doctor
                                                        immediately for a clinical exam. We will likely order an
                                                        Ultrasound or Mammogram to get more information.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does a mammogram hurt?</h4>
                                                    <p style="margin:0; font-size:15px;">It involves pressure for a few
                                                        seconds which can be uncomfortable or slightly painful for some.
                                                        To make it easier, avoid booking your mammogram the week before
                                                        your period when breasts are most sensitive.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is breast cancer hereditary?</h4>
                                                    <p style="margin:0; font-size:15px;">Only about **5-10%** of cases
                                                        are caused by inherited gene mutations (like BRCA1/2). Most
                                                        breast cancer occurs in women with NO family history. This is
                                                        why screening is important for everyone, not just those with a
                                                        family history.</p>
