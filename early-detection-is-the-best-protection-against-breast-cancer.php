                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does wearing an underwire bra cause breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** There is no scientific
                                                        evidence that bras, deodorant, or breast trauma (being hit in
                                                        the breast) cause cancer. The main risks are age, genetics, and
                                                        hormonal factors.</p>
                                                </div>
                                                <div class="faq-item"
