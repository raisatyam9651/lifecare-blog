                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is included in a "Women's Wellness Package"?</h4>
                                                    <p style="margin:0; font-size:15px;">At LifeCare, our packages
                                                        typically include a physical exam, pelvic exam, Pap smear,
                                                        breast exam/mammogram (based on age), blood count, thyroid test,
                                                        blood sugar, and lipid profile. It's designed to cover all the
