                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I have a check-up if I'm on my period?</h4>
                                                    <p style="margin:0; font-size:15px;">For a general physical exam, it
                                                        is fine. However, if you are having a Pap smear or pelvic exam,
                                                        it is best to schedule it for a time when you are not bleeding,
                                                        as the blood can sometimes interfere with the accuracy of the
                                                        cell sample.</p>
