                                                        Can a thyroid problem make it hard to get pregnant?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Thyroid hormones are
                                                        critical for ovulation and maintaining a healthy pregnancy. If
                                                        you are struggling with infertility, a Thyroid Function Test is
                                                        one of the first things we recommend at LifeCare.</p>
