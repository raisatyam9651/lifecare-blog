                                                        Is Hormonal Replacement Therapy (HRT) safe?</h4>
                                                    <p style="margin:0; font-size:15px;">For most women, HRT is very
                                                        safe and highly effective when started near the beginning of
                                                        menopause. Our doctors conduct a thorough screening of your
                                                        family history to ensure it is the right and safe choice for
                                                        you.</p>
                                                </div>
                                                <div class="faq-item"
