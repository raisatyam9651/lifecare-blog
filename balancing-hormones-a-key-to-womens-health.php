                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long does it take to see results from hormonal treatments?
                                                    </h4>
                                                    <p style="margin:0; font-size:15px;">It varies. Thyroid medication
                                                        results can be felt in days. However, because the menstrual
                                                        cycle takes a full month, it often takes 3 to 6 months of
                                                        lifestyle and medical intervention to see a significant shift in
                                                        your cycle and skin health.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can exercise cause hormonal imbalance?</h4>
                                                    <p style="margin:0; font-size:15px;">Moderate exercise is great.
                                                        However, extreme, high-impact over-training without adequate
                                                        nutrition can lead to "amenorrhea" (loss of period) as the body
                                                        shuts down non-vital systems to save energy.</p>
