                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a "Sugar Detox" help balance hormones?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes. Sugar causes insulin
                                                        spikes, and high insulin can trigger the ovaries to produce more
                                                        androgens, leading to symptoms like acne and facial hair.
                                                        Reducing refined sugar is one of the best things you can do for
                                                        your hormones.</p>
                                                </div>
                                                <div class="faq-item"
