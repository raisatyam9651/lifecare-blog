                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does drinking soda affect my bones?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Dark sodas contain
                                                        phosphoric acid, which can increase the excretion of calcium
                                                        through urine. Additionally, if you are drinking soda instead of
                                                        milk or calcium-fortified drinks, you are missing out on
                                                        essential nutrients.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a DEXA scan be done if I have a metal implant?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes. However, the metal can
                                                        interfere with the reading of that specific bone. If you have a
                                                        hip replacement, the radiologist will simply scan the other hip
                                                        or your lower spine to get an accurate reading.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How much calcium do I need every day?</h4>
                                                    <p style="margin:0; font-size:15px;">Most adults need between 1,000
                                                        and 1,200 mg of calcium per day. Women over 50 and men over 70
                                                        should aim for the higher end of that range. We always recommend
                                                        getting your calcium from food first before turning to
                                                        supplements.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is osteoporosis hereditary?</h4>
                                                    <p style="margin:0; font-size:15px;">There is a strong genetic
                                                        component. If your parents had osteoporosis or suffered from
                                                        broken bones as they aged, it is a significant indicator that
                                                        you should prioritize your bone health early and seek regular
                                                        check-ups.</p>
                                                </div>
