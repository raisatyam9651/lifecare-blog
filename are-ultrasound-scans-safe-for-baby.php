                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does an ultrasound hurt the mother or baby?</h4>
                                                    <p style="margin:0; font-size:15px;">No, an ultrasound is completely
                                                        painless. You may feel a slight pressure as the sonographer
                                                        moves the transducer over your belly, but there is no pain
                                                        involved for you or your baby.</p>
