                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How many ultrasounds should I have during a healthy pregnancy?
                                                    </h4>
                                                    <p style="margin:0; font-size:15px;">In a low-risk, healthy
                                                        pregnancy, two scans are usually standard: a dating scan (6-10
                                                        weeks) and an anatomy scan (18-22 weeks). Your doctor may
                                                        recommend more based on your specific health needs.</p>
