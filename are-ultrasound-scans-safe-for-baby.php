                                                        Can an ultrasound determine the sex of my baby?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, usually during the 20-week
                                                        anomaly scan. However, its primary purpose is anatomical health.
                                                        The baby's position can sometimes make it difficult to be 100%
                                                        certain.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Do I need a full bladder for my ultrasound?</h4>
                                                    <p style="margin:0; font-size:15px;">For very early pregnancy scans,
                                                        a full bladder helps push the uterus up, making it easier to
                                                        see. For later scans, this is usually not necessary.</p>
