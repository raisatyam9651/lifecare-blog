                                                        At what age should I start getting mammograms?</h4>
                                                    <p style="margin:0; font-size:15px;">Most health organizations
                                                        recommend starting annual mammograms at age 40. However, if you
                                                        have a family history, you should consult with our doctors about
                                                        starting as early as age 25 or 30.</p>
                                                </div>
                                                <div class="faq-item"
