                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a lump always cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">No. About 80% of lumps
                                                        investigated are benign (non-cancerous), such as cysts or
                                                        fibroadenomas. However, any new lump must be professionally
                                                        assessed by an ultrasound or mammogram to be sure.</p>
                                                </div>
                                                <div class="faq-item"
