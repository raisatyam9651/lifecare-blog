                                                        Does chemotherapy always cause hair loss?</h4>
                                                    <p style="margin:0; font-size:15px;">Not all chemotherapy drugs
                                                        cause hair loss, although many do. We also utilize specialized
                                                        medications and cooling techniques that may reduce this side
                                                        effect for certain patients.</p>
                                                </div>
                                                <div class="faq-item"
