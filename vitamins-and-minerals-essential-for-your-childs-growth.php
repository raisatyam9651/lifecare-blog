                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a child get too many vitamins?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Specifically "Fat
                                                        Soluble" vitamins (A, D, E, K). Unlike water-soluble vitamins
                                                        (like C and B), these are stored in the body's fat and can build
                                                        up to poisonous levels if over-supplemented.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What if my child only wants to eat white rice and bread?</h4>
                                                    <p style="margin:0; font-size:15px;">Picky eating is a normal phase,
                                                        but it can lead to deficiencies. Try "Nutrition
                                                        Camouflage"—blend vegetables into sauces, or add finely ground
                                                        nuts to porridge. However, if they are losing weight or have low
                                                        energy, see a LifeCare pediatrician.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does cooking vegetables destroy the vitamins?</h4>
                                                    <p style="margin:0; font-size:15px;">Heat can destroy Vitamin C.
                                                        However, some nutrients (like the Vitamin A in carrots) actually
                                                        become *more* available to the body when lightly cooked or
                                                        steamed. Avoid boiling vegetables in large amounts of water and
                                                        then throwing the water away—that's where the nutrients go!</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is brown sugar better than white sugar for kids?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Sugar is sugar. While
                                                        brown sugar has tiny trace amounts of minerals, it is not enough
                                                        to provide a health benefit. High sugar intake of any kind leads
                                                        to tooth decay and metabolic issues. Focus on getting sweetness
                                                        from whole fruits instead.</p>
                                                </div>
