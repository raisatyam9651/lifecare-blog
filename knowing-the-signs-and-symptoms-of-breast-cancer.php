                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a mammogram CAUSE breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** The radiation dose from
                                                        a mammogram is extremely low (equivalent to about 7 weeks of
                                                        natural background radiation). The health benefit of catching a
                                                        tumor years before it can be felt vastly outweighs the
                                                        negligible radiation risk.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a lump under the armpit breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">It could be. Breast tissue and
                                                        lymph nodes extend into the armpit. While it could just be an
                                                        infected hair follicle or a swollen node from a minor cold, a
                                                        *hard, fixed* lump in the armpit MUST be evaluated by a doctor.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does wearing an underwire bra cause cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** This is a persistent
                                                        myth. There is no scientific evidence that bras—whether
                                                        underwired or not—impact the lymph system or cause cancer. Wear
                                                        whatever is comfortable for you.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can breastfeeding prevent breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes, to a degree.** Studies
                                                        show that breastfeeding for a year or more cumulatively can
                                                        slightly reduce your risk of breast cancer because it reduces
                                                        the lifetime number of menstrual cycles (and thus estrogen
                                                        exposure).</p>
                                                </div>
