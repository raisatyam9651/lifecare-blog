                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should my child have a "milestone" check?</h4>
                                                    <p style="margin:0; font-size:15px;">In the first year, check-ups
                                                        usually follow the vaccination schedule (6, 10, 14 weeks, and 6,
                                                        9 months). After age 2, an annual "Wellness Visit" is sufficient
                                                        to track growth and behavioral health.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        My child is a picky eater. Should I give supplements?</h4>
                                                    <p style="margin:0; font-size:15px;">Most picky eaters still get
                                                        enough nutrients. Before starting vitamins, speak to a LifeCare
                                                        pediatrician. We can check if they have a real deficiency (like
                                                        Iron) and help you with "food bridge" strategies to expand their
                                                        palate naturally.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a high fever dangerous for the brain?</h4>
                                                    <p style="margin:0; font-size:15px;">**Generally, no.** Fever is the
                                                        body's way of fighting infection. Typical childhood fevers don't
                                                        cause brain damage. The concern with fever is usually the
                                                        underlying cause and the risk of dehydration. If the fever is
                                                        high but the child is playing and drinking, it is usually less
                                                        concerning than a low fever in a child who is lethargic.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        At what age can my child start eating solids?</h4>
                                                    <p style="margin:0; font-size:15px;">The WHO and LifeCare recommend
                                                        starting "Complementary Feeding" exactly at **6 months**.
                                                        Starting earlier can stress the baby's immature digestive system
                                                        and kidneys.</p>
                                                </div>
