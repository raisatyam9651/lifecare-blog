                                                        Is yoga good for mental health?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Yoga combines physical
                                                        movement with breath control and mindfulness. It is particularly
                                                        effective for reducing anxiety and managing the physical
                                                        symptoms of trauma.</p>
