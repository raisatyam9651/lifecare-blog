                                                        What is the best exercise for depression?</h4>
                                                    <p style="margin:0; font-size:15px;">**Aerobic exercise** (walking,
                                                        swimming, cycling) has the strongest evidence for depression
                                                        management. However, any movement you enjoy is the "best" one
                                                        because you are more likely to stick with it consistently.</p>
