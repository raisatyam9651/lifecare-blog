                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Did vaccines cause my child's autism?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** This is a debunked
                                                        myth. Massive global studies involving millions of children have
                                                        consistently proven that there is NO link between vaccinations
                                                        and autism. Autism is a biological difference in brain
                                                        development that occurs before a child is even born.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can autism be "cured" with diet?</h4>
                                                    <p style="margin:0; font-size:15px;">There is no "cure" for autism
                                                        because it is not an illness. However, many children with autism
                                                        also have sensitive digestive systems. A healthy, balanced diet
                                                        can improve their comfort and behavior, but it will not "remove"
                                                        their autism.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a child with autism go to a "normal" school?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes**, many can! This is
                                                        called "mainstreaming" or "inclusion." With the right support
                                                        (like a shadow teacher or extra time on exams), many children on
                                                        the spectrum thrive in regular academic environments.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is autism hereditary?</h4>
                                                    <p style="margin:0; font-size:15px;">Genetics plays a major role. If
                                                        there is autism in the family, the chance of another child being
                                                        on the spectrum is higher. However, it's also common for a child
                                                        to be the first in their family to be diagnosed.</p>
                                                </div>
