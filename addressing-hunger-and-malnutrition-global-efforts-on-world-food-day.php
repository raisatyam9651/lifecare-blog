                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is "Stunting" and why is it dangerous?</h4>
                                                    <p style="margin:0; font-size:15px;">Stunting is the impaired growth
                                                        and development that children experience from poor nutrition. It
                                                        is dangerous because it often leads to irreversible physical and
                                                        cognitive damage.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is "Hidden Hunger"?</h4>
                                                    <p style="margin:0; font-size:15px;">Hidden hunger refers to
                                                        micronutrient deficiencies. A person might be getting enough
                                                        calories to not feel hungry, but they are deficient in essential
                                                        vitamins and minerals like Iron, Zinc, or Iodine.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I take supplements to avoid malnutrition?</h4>
                                                    <p style="margin:0; font-size:15px;">A balanced diet of whole foods
                                                        is the best source of nutrients. However, if you have a
                                                        diagnosed deficiency, your doctor may recommend specific
                                                        supplements. Never self-diagnose.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How can I tell if my child is malnourished?</h4>
                                                    <p style="margin:0; font-size:15px;">Signs include slow weight gain,
                                                        lack of energy, poor concentration, and frequent illness.
                                                        Regular "Mother and Child" clinics at LifeCare use growth charts
                                                        to monitor this accurately.</p>
                                                </div>
