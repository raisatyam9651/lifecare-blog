                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can Type 2 Diabetes be cured?</h4>
                                                    <p style="margin:0; font-size:15px;">**No**, currently there is no
                                                        permanent "cure." However, it can be put into **Remission**.
                                                        This means your blood sugar levels are back in the healthy range
                                                        without the need for medication, usually achieved through
                                                        significant weight loss and lifestyle changes.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is local brown ugali better than white?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Brown (whole grain)
                                                        ugali has more fiber, which slows down digestion and stops your
                                                        blood sugar from spiking as fast as it does with highly refined
                                                        white ugali. However, portion control is still important!</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What are the signs of low blood sugar (Hypo)?</h4>
                                                    <p style="margin:0; font-size:15px;">Hypoglycemia can make you feel
                                                        shaky, sweaty, confused, or extremely hungry. If this happens,
                                                        follow the **15-15 Rule**: eat 15 grams of fast-acting sugar
                                                        (like 3 glucose tablets or half a cup of juice), wait 15
                                                        minutes, and re-check your sugar.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I check my feet?</h4>
                                                    <p style="margin:0; font-size:15px;">**Every single day.** Diabetes
                                                        can cause numbness (neuropathy), so you might not feel a cut or
                                                        a blister. Inspect your feet for redness, swelling, or sores
                                                        that aren't healing. Early detection prevents serious infections
                                                        and amputations.</p>
                                                </div>
