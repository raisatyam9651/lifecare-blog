                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does my child need a multivitamin?</h4>
                                                    <p style="margin:0; font-size:15px;">**Ideally, no.** Most children
                                                        can get all their nutrients from a balanced diet. However, if
                                                        your child is extremely fussy or has an underlying condition, a
                                                        pediatrician at LifeCare may recommend a supplement after seeing
                                                        their lab results.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        When should my child start drinking milk?</h4>
                                                    <p style="margin:0; font-size:15px;">Whole cow's milk should only be
                                                        introduced after the age of 12 months. Before that, breastmilk
                                                        or formula is the only safe option for their developing kidneys
                                                        and digestive system.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        My child is always "hyper" after certain foods. Is it sugar?
                                                    </h4>
                                                    <p style="margin:0; font-size:15px;">While "sugar highs" are debated
                                                        scientifically, many children do experience energy spikes from
                                                        refined sugars and certain food dyes. Observe their patterns and
                                                        try replacing sugary snacks with protein-rich ones (like yogurt
                                                        or nuts) to see if their behavior stabilizes.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is juice better than soda?</h4>
                                                    <p style="margin:0; font-size:15px;">**Slightly.** While juice has
                                                        vitamins, it often has as much sugar as soda and lacks the fiber
                                                        of whole fruit. It is always better to eat the whole orange
                                                        rather than drink the juice. If giving juice, dilute it with 50%
                                                        water.</p>
                                                </div>
