                            another doctor to tell me the results?</h4>
                          <p style="margin:0; font-size:15px;">The radiologist is an expert in images, but your
                            referring doctor (the one who ordered the test) is the expert in *you*. They need to take
                            the radiologist's findings and combine them with your symptoms and physical exam to decide
                            the best treatment plan.</p>
