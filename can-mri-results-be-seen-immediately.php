                            mean the results are bad?</h4>
                          <p style="margin:0; font-size:15px;">No. A longer wait time usually just means the radiologist
                            has a large volume of scans to read, or they are taking extra time to compare your scan with
                            old records. It is not an indicator of your health status.</p>
