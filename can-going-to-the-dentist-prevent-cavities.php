                                                        Is a cleaning painful?</h4>
                                                    <p style="margin:0; font-size:15px;">For most patients, a cleaning
                                                        is not painful. You might feel some "tickling" or slight
                                                        vibration from the tools. If you have sensitive teeth or deep
                                                        gum pockets, our hygienists can use a numbing gel to ensure you
                                                        are 100% comfortable.</p>
