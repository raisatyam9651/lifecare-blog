                                                        Are dental sealants only for kids?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** While they are very
                                                        common for children whose permanent molars are just coming in,
                                                        adults who do not have decay or fillings in their molars can
                                                        also benefit from sealants to protect their healthy enamel.</p>
