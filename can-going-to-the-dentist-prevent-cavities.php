                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a cavity go away on its own?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Once a hole has formed
                                                        in the enamel, it cannot "heal" on its own. It will only grow
                                                        larger. However, a pre-cavity (white spot) can sometimes be
                                                        reversed with professional fluoride treatments before it becomes
                                                        a full hole.</p>
