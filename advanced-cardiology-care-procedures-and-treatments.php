                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long does a typical angioplasty take?</h4>
                                                    <p style="margin:0; font-size:15px;">A routine coronary angioplasty
                                                        usually takes between 30 to 90 minutes. However, it can take
                                                        longer depending on the complexity of the blockage.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are stents permanent?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, once a stent is placed in
                                                        an artery, it is permanent. Over time, the lining of the artery
                                                        grows over the stent, incorporating it into the vessel wall.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is the recovery time for bypass surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">Most patients stay in the
                                                        hospital for 5 to 7 days after CABG. Full recovery, where you
                                                        can return to normal physical activity, usually takes 6 to 12
                                                        weeks.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a CT scan replace an invasive angiography?</h4>
                                                    <p style="margin:0; font-size:15px;">In many cases, yes. A Cardiac
                                                        CT is excellent for "ruling out" coronary artery disease.
                                                        However, if a significant blockage is found that needs
                                                        treatment, an invasive angiography is still required to perform
                                                        the angioplasty.</p>
                                                </div>
