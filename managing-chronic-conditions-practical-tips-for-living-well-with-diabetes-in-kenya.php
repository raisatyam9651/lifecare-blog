                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can Type 2 Diabetes be reversed?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes and No.** We use the term
                                                        "Remission." With significant weight loss and exercise, many
                                                        people can reach normal sugar levels without medication.
                                                        However, the underlying "tendency" remains, so healthy habits
                                                        must be permanent.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is honey better than sugar for diabetics?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** While honey has some
                                                        nutrients, the body processes it exactly like sugar. It will
                                                        cause a spike in your blood glucose. Use it very sparingly or
                                                        not at all if your sugar is poorly controlled.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is a "Hypo" and what do I do?</h4>
                                                    <p style="margin:0; font-size:15px;">Hypoglycemia (Low blood sugar)
                                                        is when your sugar drops below 4.0 mmol/L. You may feel shaky,
                                                        sweaty, and confused. The "Rule of 15" applies: Eat 15g of
                                                        fast-acting sugar (like 3 glucose tablets or half a cup of
                                                        juice), wait 15 minutes, and re-test. Always carry a sugar
                                                        source with you if you are on insulin or certain tablets.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is brown bread safe to eat in unlimited amounts?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Brown bread is better
                                                        because it has fiber, but it is still a carbohydrate. Two slices
                                                        of brown bread will raise your blood sugar. Portion control is
                                                        always more important than the "color" of the food.</p>
                                                </div>
