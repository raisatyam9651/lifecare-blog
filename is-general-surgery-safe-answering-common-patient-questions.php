                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        When can I drive after surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">You should not drive until you
                                                        are no longer taking strong pain medications and you can
                                                        comfortably perform an "emergency stop" movement. For minor
                                                        surgeries, this might be 3-5 days; for major surgeries, it may
                                                        be 2-3 weeks.</p>
