                                                        Does surgery mean I will be in pain for months?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Most post-surgical pain
                                                        is managed very well with modern medications. In fact, most
                                                        general surgery patients are back to light activity within 7-10
