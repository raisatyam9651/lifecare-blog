                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long will my scar be?</h4>
                                                    <p style="margin:0; font-size:15px;">It depends on the type of
                                                        surgery. Laparoscopic scars are tiny (less than 1cm). Open
                                                        surgeries require larger incisions, but our surgeons use
