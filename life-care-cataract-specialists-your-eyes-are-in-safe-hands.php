                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will the cataract come back?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Once the natural lens
                                                        is removed and replaced with an artificial one, a cataract can
                                                        never grow again. Some patients develop a "secondary cataract"
                                                        (clouding of the capsule holding the lens), but this is easily
                                                        fixed in 2 minutes with a simple laser in the clinic.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I have both eyes done at once?</h4>
                                                    <p style="margin:0; font-size:15px;">We generally recommend doing
                                                        one eye at a time, usually about 1-2 weeks apart. This ensures
                                                        that you have one "good" eye to use while the first one is
                                                        healing, and it is a safer approach for the patient's mobility.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will I be awake during surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Most patients are
                                                        awake but very relaxed. You will not see the instruments, and
                                                        you will not feel any pain. The numbing drops we use are very
                                                        effective. Most patients describe seeing beautiful "lights and
                                                        colors" during the 15-minute procedure.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How soon can I drive after surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">Most patients' vision is clear
                                                        enough to drive within 48 to 72 hours. However, we officially
                                                        recommend waiting until your first post-operative check-up
                                                        (usually 24 hours after surgery) when your LifeCare specialist
                                                        will confirm your vision is safe for the road.</p>
                                                </div>
