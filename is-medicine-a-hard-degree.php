                Frequently Asked Questions (FAQs)</h3>
            <div class="faq-section" style="margin-bottom:20px;">
                <div class="faq-item"
                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                    <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                        What subjects are most important in high school?</h4>
                    <p style="margin:0; font-size:15px;">In Kenya, you must excel in the
                        sciences—Biology and Chemistry are mandatory. You also need
                        strong marks in Mathematics or Physics and English/Kiswahili to
                        meet the cluster requirements for medical school entry.</p>
                </div>
                <div class="faq-item"
                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                    <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                        Can a medical student have a social life?</h4>
                    <p style="margin:0; font-size:15px;">**Yes, but it requires extreme
                        time management.** You have to be very intentional about your
                        breaks. Many medical students play sports, play instruments, or
                        engage in student politics, but they have to be
                        "hyper-organized" to make it work.</p>
                </div>
                <div class="faq-item"
                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                    <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                        Is a medical degree abroad recognized in Kenya?</h4>
                    <p style="margin:0; font-size:15px;">If you study outside Kenya, you
                        must ensure the university is recognized by the Kenya Medical
                        Practitioners and Dentists Council (KMPDC). Upon returning, you
                        will likely need to sit a licensure exam before you can practice
                        here.</p>
                </div>
                <div class="faq-item"
                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                    <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                        How long after the degree can I specialize?</h4>
                    <p style="margin:0; font-size:15px;">After your 6-year degree and
                        1-year internship, you must work as a Medical Officer (MO) for
                        at least 2 years. After that, you can apply for a residency
                        (Master of Medicine) which takes another 3 to 5 years depending
                        on the specialty.</p>
                </div>
