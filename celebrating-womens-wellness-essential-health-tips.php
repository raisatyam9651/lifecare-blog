                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I have a Pap smear?</h4>
                                                    <p style="margin:0; font-size:15px;">For most women with normal
                                                        results, we recommend a Pap smear every **3 years**, or a
                                                        co-test (Pap + HPV test) every **5 years** between the ages of
                                                        30 and 65.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        I feel fine. Do I still need a wellness check?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Conditions like high
                                                        blood pressure, cervical cancer, and osteoporosis are called
                                                        "silent" because they have no symptoms in the early stages. The
                                                        goal of a check-up is to *keep* you feeling fine.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can nutrition affect my periods?</h4>
                                                    <p style="margin:0; font-size:15px;">**Absolutely.** Changes in
                                                        weight, extreme dieting, or lack of healthy fats can disrupt
                                                        your hormones and cause irregular or missing periods. A balanced
                                                        diet is critical for reproductive harmony.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is menopause weight gain inevitable?</h4>
                                                    <p style="margin:0; font-size:15px;">It is common, but not
                                                        inevitable. As estrogen drops, your body stores fat differently
                                                        (mostly in the belly). By increasing protein, focusing on
                                                        strength training, and managing stress, you can maintain a
                                                        healthy weight through menopause.</p>
                                                </div>
