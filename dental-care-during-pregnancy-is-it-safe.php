                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I have a tooth pulled while pregnant?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** If a tooth is infected
                                                        or causing severe pain, an extraction is safer than allowing an
                                                        infection to spread. We will use safe anesthesia and ensure you
                                                        are comfortable throughout the procedure.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does pregnancy actually "suck calcium" from teeth?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** This is a persistent
                                                        myth. Your body will take calcium from your *bones* if your diet
                                                        is deficient, but not from your teeth. Pregnancy-related tooth
                                                        decay is caused by increased sugar cravings, morning sickness
                                                        acid, and changes in oral hygiene, not calcium depletion.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I avoid mouthwash?</h4>
                                                    <p style="margin:0; font-size:15px;">You should choose an
                                                        alcohol-free mouthwash. Mouthwashes containing alcohol can be
                                                        drying and irritating to your already sensitive pregnant gums.
                                                        Look for fluoride-based, alcohol-free options.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will my gingivitis go away after I give birth?</h4>
                                                    <p style="margin:0; font-size:15px;">Usually, yes. As your hormone
                                                        levels return to normal, the sensitivity of your gums will
                                                        decrease. However, it is vital to have a professional cleaning
                                                        at LifeCare post-birth to ensure no permanent damage was done to
                                                        the supporting bone during the pregnancy.</p>
                                                </div>
