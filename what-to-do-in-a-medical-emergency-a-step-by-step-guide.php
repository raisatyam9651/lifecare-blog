                      <h1>What to Do in a Medical Emergency: A Step-by-Step Survival Guide</h1>

                      <p>A medical emergency is one of the most stressful experiences anyone can face. Whether it's a
                        sudden collapse, a car accident, or an acute chest pain, the first few minutes—the "Golden
                        Hour"—are often the most critical. In these moments, panic is your greatest enemy, and clear
                        knowledge is your most powerful ally.</p>

                      <p>At LifeCare Hospitals Kenya, our emergency departments are staffed 24/7 with trauma
                        specialists. However, we are the second line of defense. *You* are the first. Knowing what to do
                        before the ambulance arrives can be the difference between a full recovery and a tragic outcome.
                        in this guide, we provide a step-by-step framework for handling a medical crisis in Kenya.</p>

                      <div class="emergency-steps">
                        <h3>Step 1: The DRABC Protocol</h3>
                        <p>In any emergency, follow this immediate sequence:</p>
                        <ul>
                          <li><strong>D - Danger:</strong> Ensure the area is safe for you to help. Don't become a
                            second casualty (e.g., watch for traffic or electrical wires).</li>
                          <li><strong>R - Response:</strong> Check if the person is conscious. Tap their shoulder and
                            shout, "Are you okay?"</li>
                          <li><strong>A - Airway:</strong> If they aren't responding, check if their airway is clear.
                            Gently tilt their head back to open the throat.</li>
                          <li><strong>B - Breathing:</strong> Look, listen, and feel for breath for 10 seconds. If they
                            aren't breathing, call for help immediately.</li>
                          <li><strong>C - Circulation:</strong> Check for a pulse and look for severe bleeding that
                            needs to be stopped with direct pressure.</li>
                        </ul>
                      </div>

                      <h2>2. Recognizing Life-Threatening Emergencies</h2>
                      <p>Knowing when to call an ambulance is vital. Here are the "Major Red Flags":</p>

                      <h3 style="color:#00b2e3;">Heart Attack Signs</h3>
                      <p>Look for heavy pressure or a "crushing" sensation in the center of the chest, pain radiating
                        down the left arm or into the jaw, and cold sweats. In women, symptoms can be more subtle, like
                        extreme fatigue or nausea.</p>

                      <h3 style="color:#00b2e3;">Stroke (Think F.A.S.T.)</h3>
                      <ul>
                        <li><strong>F - Face:</strong> Is one side of the face drooping? Ask them to smile.</li>
                        <li><strong>A - Arms:</strong> Can they lift both arms? Does one drift downward?</li>
                        <li><strong>S - Speech:</strong> Is their speech slurred? Can they repeat a simple sentence?
                        </li>
                        <li><strong>T - Time:</strong> If you see any of these signs, it is time to call for help
                          immediately.</li>
                      </ul>

                      <h2>3. Calling for Help in Kenya</h2>
                      <p>Keep these numbers saved in your phone and written on your fridge:
                      <ul>
                        <li><strong>Nationwide Emergency:</strong> 999 / 112 / 911</li>
                        <li><strong>LifeCare Emergency Line:</strong> +254 730 950 000</li>
                        <li><strong>St. John Ambulance:</strong> 0721 225252</li>
                      </ul>
                      When you call, stay calm. Give your exact location (use landmarks or a PIN drop via WhatsApp),
                      describe the patient's condition, and don't hang up until the operator tells you to.</p>

                      <h2>4. What to Do While Waiting for the Ambulance</h2>
                      <ul>
                        <li><strong>Stay with the Patient:</strong> Keep them calm and talk to them. Reassurance lowers
                          heart rate and stress.</li>
                        <li><strong>Don't Move Them:</strong> Unless they are in immediate danger (like a fire), don't
                          move a trauma victim, as you could cause permanent spinal damage.</li>
                        <li><strong>Collect Information:</strong> Gather the patient's ID, any medications they are
                          taking, and their NHIF or insurance card. This saves vital minutes at the hospital.</li>
                        <li><strong>Clear the Path:</strong> Make sure the gate is open, pets are locked away, and
                          someone is waiting outside to flag down the ambulance.</li>
                      </ul>

                      <h2>5. What NOT to Do</h2>
                      <ul>
                        <li><strong>Don't Give Food or Water:</strong> If the patient needs emergency surgery, food in
                          their stomach makes anesthesia dangerous.</li>
                        <li><strong>Don't Wait to "See if it Gets Better":</strong> In emergencies like heart attacks or
                          strokes, every minute equals dead tissue. It is better to have a "false alarm" than to arrive
                          too late.</li>
                        <li><strong>Don't Drive Yourself:</strong> If you are experiencing chest pain or severe
                          dizziness, do not get behind the wheel. You could crash and hurt others. Wait for the
                          ambulance or have someone else drive.</li>
                      </ul>

                      <h2>6. LifeCare's Emergency Response</h2>
                      <p>When you arrive at LifeCare Hospitals Kenya, we use a system called **Triage**. This means we
                        treat the most life-threatening cases first, regardless of when they arrived. Our ER is equipped
                        with ventilators, defibrillators, and direct access to imaging (CT/X-ray) to ensure we can
                        diagnose and stabilize you within minutes.</p>

                      <h2>Conclusion: Preparation is the Best Medicine</h2>
                      <p>No one expects an emergency, but everyone can prepare for one. Take a basic first-aid course,
                        keep a first-aid kit in your car, and ensure your family knows where the nearest LifeCare
                        Hospital is located. Your quick thinking and calm actions could be the reason a loved one comes
                        home safely.</p>
                      <p>At LifeCare, we are ready when you need us most. We hope you never have to visit our ER, but if
                        you do, your life is in the safest, fastest hands in Kenya. Let's work together to keep our
                        community safe.</p>

