                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #e91e63;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does radiation from mammograms cause cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">The amount of radiation used in
                                                        a modern mammogram is very small—comparable to the amount of
                                                        natural background radiation a person receives over seven weeks.
                                                        The benefit of finding cancer early far outweighs the tiny risk
                                                        from the X-ray.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #e91e63;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Do men get breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** While much rarer than
                                                        in women (about 1% of all cases), men do have breast tissue and
                                                        can develop breast cancer. Men should also be aware of lumps or
                                                        changes in their chest area and report them to a doctor
                                                        immediately.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #e91e63;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are all breast lumps cancerous?</h4>
                                                    <p style="margin:0; font-size:15px;">No. In fact, about 8 out of 10
                                                        breast lumps are benign (not cancer). They can be cysts,
                                                        fibroadenomas, or other non-cancerous changes. However, you
                                                        cannot tell by "feeling" them—only a medical exam and imaging
                                                        can provide certainty.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #e91e63;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long does a mammogram take?</h4>
                                                    <p style="margin:0; font-size:15px;">The entire procedure usually
                                                        takes about 20 minutes. Each breast is compressed for only a few
                                                        seconds while the X-ray is taken. Many women find the
                                                        compression slightly uncomfortable, but it is not typically
                                                        painful.</p>
                                                </div>
