                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is pneumonia contagious?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** The bacteria and
                                                        viruses that cause pneumonia can be spread through the air via
                                                        coughs or sneezes. However, "walking pneumonia" is less
                                                        contagious than the flu. Good handwashing and covering your
                                                        mouth are essential for prevention.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I treat pneumonia at home?</h4>
                                                    <p style="margin:0; font-size:15px;">Mild cases can often be treated
                                                        at home with antibiotics and rest. However, if the patient is a
                                                        child, an elderly person, or anyone with a low oxygen level,
                                                        hospitalization is required for IV antibiotics and oxygen
                                                        therapy. Our doctors will make that clinical determination.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long does it take to recover?</h4>
                                                    <p style="margin:0; font-size:15px;">Most symptoms like fever and
                                                        chest pain improve within a week. However, the fatigue and cough
                                                        can linger for a month or more. It is important to finish the
                                                        full course of antibiotics even if you start feeling better.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can you get pneumonia more than once?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Because there are many
                                                        different types of bacteria and viruses that cause the
                                                        infection, you can get it again. This is why regular vaccination
                                                        and maintaining lung health are so important.</p>
                                                </div>
