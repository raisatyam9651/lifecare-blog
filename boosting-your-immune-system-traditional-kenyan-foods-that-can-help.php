                        Frequently Asked Questions (FAQs)</h3>
                      <div class="faq-section" style="margin-bottom:20px;">
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Does boiling
                            vegetables too long destroy the nutrients?</h4>
                          <p style="margin:0; font-size:15px;">Yes, especially Vitamin C. To keep the nutrients in your
                            Managu or Saget, try to steam them or "sauté" them quickly rather than boiling them in large
                            amounts of water for hours. If you must boil them, use the water in a soup so you don't pour
                            out the minerals.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is Ugali and milk
                            enough for a balanced diet?</h4>
                          <p style="margin:0; font-size:15px;">While it's a great base, it lacks variety. Adding
                            traditional vegetables like Terere and a source of healthy fat like avocado makes it much
                            more balanced. Variety is the secret to a strong immune system.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can food replace
                            medicine?</h4>
                          <p style="margin:0; font-size:15px;">Food is preventative and supports healing, but it cannot
                            replace "acute" medical care. If you are already sick, you need clinical treatment. Think of
                            food as the "fortress wall" and medicine as the "soldiers" who fight the battle when the
                            wall is breached.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Are indigenous
                            grains better for diabetics?</h4>
                          <p style="margin:0; font-size:15px;">Yes. Wimbi and Sorghum have a lower Glycemic Index (GI)
                            than white maize or rice. This means they cause a much slower rise in blood sugar, which is
                            much safer and healthier for people managing diabetes.</p>
