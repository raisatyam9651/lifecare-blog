                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does my employer HAVE to provide PPE?</h4>
                                                    <p style="margin:0; font-size:15px;">Under the Occupational Safety
                                                        and Health Act (OSHA) of Kenya, employers have a legal
                                                        obligation to provide a safe working environment, which includes
                                                        providing necessary protective gear and training at no cost to
                                                        the employee.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can workplace stress be considered an occupational hazard?</h4>
                                                    <p style="margin:0; font-size:15px;">**Absolutely.** Chronic stress
                                                        leads to high blood pressure, heart disease, and mental health
                                                        crises. A "safe" workplace must also be a "mentally safe" one.
                                                        LifeCare offers corporate counseling to help teams manage
                                                        workplace pressure.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should we have occupational check-ups?</h4>
                                                    <p style="margin:0; font-size:15px;">For high-risk industries
                                                        (mining, chemical manufacturing, heavy construction), we
                                                        recommend a full occupational review every 12 months. For
                                                        office-based environments, a biennial review (every 2 years) is
                                                        usually sufficient to monitor for chronic issues like spinal
                                                        problems or eye strain.</p>
                                                </div>
