                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What should I do if I am injured at work?</h4>
                                                    <p style="margin:0; font-size:15px;">**First:** Seek medical
                                                        attention immediately at the nearest LifeCare emergency center.
                                                        **Second:** Report the incident to your supervisor to ensure a
                                                        proper "Incident Report" is filed. This is vital for your health
                                                        records and any future insurance or WIBA (Work Injury Benefits
                                                        Act) claims.</p>
