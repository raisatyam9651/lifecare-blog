                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will I be sore after the session?</h4>
                                                    <p style="margin:0; font-size:15px;">It is very common to feel a bit
                                                        of muscle soreness for 24-48 hours after your first few
                                                        sessions—much like after a good workout. This is a normal part
                                                        of the body adjusting to new patterns of movement and is not a
                                                        cause for alarm.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How many sessions will I need?</h4>
                                                    <p style="margin:0; font-size:15px;">This depends entirely on your
                                                        diagnosis. A simple muscle strain might resolve in 3 sessions,
                                                        while post-surgical rehab or chronic back pain might require
                                                        10-12 sessions over several weeks. Your LifeCare therapist will
                                                        give you a clear roadmap after your first assessment.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does physiotherapy replace surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">In many cases, **yes.** For
                                                        conditions like slipped discs, rotator cuff tears, or meniscal
                                                        injuries, "conservative management" (physiotherapy) is often
                                                        tried first. Many patients improve so much that surgery is no
                                                        longer necessary. However, if surgery *is* required, physio is
                                                        essential for a safe recovery.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is dry needling painful?</h4>
                                                    <p style="margin:0; font-size:15px;">Dry needling uses very thin
                                                        needles. You might feel a tiny "prick" when it enters, followed
                                                        by a "twitch" in the muscle. It is not generally painful, but it
                                                        can feel quite "odd" or intense for a split second. Most
                                                        patients find the relief it provides to be well worth the
                                                        sensation.</p>
                                                </div>
