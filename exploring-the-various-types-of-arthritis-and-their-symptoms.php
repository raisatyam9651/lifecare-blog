                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a "special diet" cure arthritis?</h4>
                                                    <p style="margin:0; font-size:15px;">**No**, but it can help manage
                                                        it. An anti-inflammatory diet (rich in Omega-3 fish, ginger,
                                                        turmeric, and vegetables) can reduce RA symptoms. Specific
                                                        avoids (like red meat and alcohol) are essential for managing
                                                        Gout.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I stop exercising if my joints hurt?</h4>
                                                    <p style="margin:0; font-size:15px;">**Generally, no.** While you
                                                        should avoid high-impact activities during a "flare," movement
                                                        is actually essential for arthritis health. "Movement is
                                                        Lotion"—it keeps the joint fluid circulating. Low-impact
                                                        exercises like swimming or cycling are usually recommended.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is "cracking" your knuckles bad for arthritis?</h4>
                                                    <p style="margin:0; font-size:15px;">Scientific studies have shown
                                                        that cracking knuckles does not cause arthritis. The "pop" you
                                                        hear is just gas bubbles moving in the joint fluid. However, if
                                                        cracking causes pain or swelling, you should have your joint
                                                        checked.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can weather (like cold mornings in Nairobi) affect arthritis?
                                                    </h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** While cold weather
                                                        doesn't *cause* arthritis, changes in barometric pressure can
                                                        cause joint tissues to expand and contract, which increases pain
                                                        and stiffness for many patients. Keeping joints warm with
                                                        clothing or heat patches often helps.</p>
                                                </div>
