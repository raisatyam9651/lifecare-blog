                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is a "Silent Heart Attack"?</h4>
                                                    <p style="margin:0; font-size:15px;">A silent heart attack is one
                                                        where the person feels no chest pain. Because diabetes can
                                                        damage the nerves that carry pain signals from the heart, the
                                                        patient may only feel slightly "out of sorts," nauseous, or
                                                        suddenly exhausted. This makes regular heart screenings vital
                                                        for diabetics.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does Type 1 or Type 2 have more heart risk?</h4>
                                                    <p style="margin:0; font-size:15px;">Both types significantly
                                                        increase the risk. However, because Type 2 diabetes is often
                                                        associated with obesity, high blood pressure, and abnormal
                                                        lipids (Metabolic Syndrome), Type 2 patients often have a more
                                                        complex profile of cardiovascular risk factors.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a heart healthy diet reverse diabetes?</h4>
                                                    <p style="margin:0; font-size:15px;">While Type 1 cannot be
                                                        "reversed," many patients with Type 2 diabetes can achieve
                                                        remission through significant weight loss and a strict,
                                                        high-fiber, low-sugar diet. Even if not reversed, such a diet is
                                                        the single best way to protect your heart.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is Diabetic Cardiomyopathy?</h4>
                                                    <p style="margin:0; font-size:15px;">This is a structural change
                                                        where the heart muscle cells are damaged directly by high sugar
                                                        and insulin resistance, independent of artery disease. The heart
                                                        becomes thick and loses its ability to relax and fill with blood
                                                        properly.</p>
                                                </div>
