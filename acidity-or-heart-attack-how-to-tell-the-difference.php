                                            <h1>Acidity or Heart Attack: How to Tell the Difference</h1>

                                            <p>It’s late at night, and you suddenly feel a sharp, burning sensation in
                                                your chest. Your mind starts racing: Is it just the spicy food I had for
                                                dinner, or is this something more sinister? This confusion is incredibly
                                                common. Because the esophagus (the food pipe) and the heart are located
                                                close to each other in the chest, they share common nerve pathways. This
                                                means that acidity—often called heartburn—and a heart attack can feel
                                                remarkably similar.</p>

                                            <p>However, distinguishing between the two is a matter of life and death. At
                                                LifeCare Hospitals Kenya, our emergency departments frequently see
                                                patients who either panicked over mild indigestion or, more dangerously,
                                                waited too long to seek help for a cardiac event thinking it was just
                                                "gas." In this guide, we break down the subtle and not-so-subtle
                                                differences between acidity and a heart attack to help you make the
                                                right call.</p>

