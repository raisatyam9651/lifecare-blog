                                            <h2>When it is a Medical Emergency</h2>
                                            <p>If you experience chest pain that lasts for more than 10 to 15 minutes,
                                                or if the pain is accompanied by shortness of breath and sweating, you
                                                must treat it as a medical emergency. Do not drive yourself to the
                                                hospital; call for an ambulance or have someone drive you.</p>

                                            <h3>Diagnostic Procedures at LifeCare Hospitals</h3>
                                            <p>When you arrive at LifeCare with chest pain, we follow a rapid-response
                                                protocol:</p>
                                            <ul>
                                                <li><strong>ECG (Electrocardiogram):</strong> Done within minutes to
                                                    check for electrical abnormalities in the heart.</li>
                                                <li><strong>Troponin Test:</strong> A blood test to detect specific
                                                    proteins released when the heart muscle is damaged.</li>
                                                <li><strong>Endoscopy:</strong> If the heart is cleared, our
                                                    gastroenterologists may perform an endoscopy to examine the
                                                    esophagus for signs of GERD or ulcers.</li>
                                            </ul>

                                            <h2>Living with Confidence: Prevention Strategies</h2>
                                            <p>Regardless of whether your pain is from acidity or your heart, the
                                                underlying causes are often manageable lifestyle factors. Improving your
                                                diet, managing your weight, and dealing with chronic stress will benefit
                                                both your digestive and cardiovascular systems.</p>
                                            <p>For acidity, avoid eating within three hours of bedtime and limit intake
                                                of caffeine and fatty foods. For heart health, ensure you are getting
                                                regular aerobic exercise and monitoring your cholesterol levels annually
                                                at a LifeCare multi-specialty facility.</p>

                                            <h2>Conclusion: Better Safe than Sorry</h2>
                                            <p>The human body is complex, and symptoms can be misleading. While we hope
                                                it is "just gas," our medical team at LifeCare Hospitals Kenya is
                                                trained to prepare for the worst while hoping for the best. Never feel
                                                embarrassed about coming to the ER for a heart attack that turns out to
                                                be acidity. We would much rather send you home with a prescription for
                                                antacids than have you stay home and suffer a life-altering cardiac
                                                event.</p>
                                            <p>With our 24/7 emergency care and world-class specialists, LifeCare is
                                                your safety net in times of medical uncertainty. Stay informed, listen
                                                to your body, and never ignore the signs.</p>

