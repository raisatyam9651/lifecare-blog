                                                        Are women's heart attack symptoms different?</h4>
                                                    <p style="margin:0; font-size:15px;">Often, yes. Women are more
                                                        likely to experience nausea, back pain, and extreme fatigue
                                                        rather than the "classic" crushing chest pain.</p>
