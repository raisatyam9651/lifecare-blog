                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can Osteoporosis be reversed?</h4>
                                                    <p style="margin:0; font-size:15px;">While it is difficult to "fully
                                                        reverse" it, modern medications and lifestyle changes can
                                                        significantly increase bone density and, more importantly,
                                                        drastically reduce your risk of a future fracture. Early
                                                        intervention is key.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does drinking soda weaken bones?</h4>
                                                    <p style="margin:0; font-size:15px;">Some studies suggest that the
                                                        phosphoric acid in dark colas can lead to calcium loss. More
                                                        importantly, people who drink a lot of soda often drink less
                                                        milk or calcium-enriched water, leading to a dietary deficiency.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are plant-based milks good for bones?</h4>
                                                    <p style="margin:0; font-size:15px;">Naturally, soy or almond milks
                                                        do not have much calcium. However, many brands sold in Kenya are
                                                        **fortified** with calcium and Vitamin D. Always check the label
                                                        to ensure you are getting the minerals your bones need.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is walking enough exercise for bone health?</h4>
                                                    <p style="margin:0; font-size:15px;">Walking is great, but adding
                                                        some level of resistance (like carrying light groceries or doing
                                                        wall push-ups) is even better. The more you "tug" on the muscles
                                                        and bones, the more your body prioritizes maintaining that bone
                                                        density.</p>
