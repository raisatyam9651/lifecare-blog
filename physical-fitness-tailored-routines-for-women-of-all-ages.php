                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will lifting weights make me look "manly"?</h4>
                                                    <p style="margin:0; font-size:15px;">**Absolutely not.** Women do
                                                        not have the levels of testosterone required to build massive
                                                        bulk naturally. Lifting weights will give you a "toned" look,
                                                        but more importantly, it will make your bones strong and your
                                                        metabolism fast.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How soon can I exercise after a C-section?</h4>
                                                    <p style="margin:0; font-size:15px;">You should wait at least 6 to 8
                                                        weeks and get clearance from your OBGYN at LifeCare. Start with
                                                        very gentle walking and specialized "Deep Core" breathing. Avoid
                                                        crunches or heavy lifting until your internal tissues have fully
                                                        healed.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is walking enough for fitness in my 60s?</h4>
                                                    <p style="margin:0; font-size:15px;">Walking is fantastic for your
                                                        heart and mood, but for healthy aging, you **must** combine it
                                                        with some form of resistance (like lifting light groceries or
                                                        using bands) to protect your muscles from "Sarcopenia" (muscle
                                                        wasting).</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        I have knee pain. Should I stop exercising?</h4>
                                                    <p style="margin:0; font-size:15px;">**Rarely.** Usually, knee pain
                                                        is caused by weak muscles around the joint. Stopping movement
                                                        often makes it worse. A LifeCare physiotherapist can show you
                                                        "low-impact" exercises (like swimming or specific leg lifts)
                                                        that strengthen the joint without causing pain.</p>
                                                </div>
