                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can medication
                            help prevent low BP during dialysis?</h4>
                          <p style="margin:0; font-size:15px;">Yes. Some patients are prescribed medications like
                            midodrine before a session to help maintain blood pressure. However, these are only used
                            under strict nephrologist supervision.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Does drinking
                            water during dialysis help?</h4>
                          <p style="margin:0; font-size:15px;">Generally, no. Drinking fluid during treatment adds to
                            the volume the machine has to remove. If your BP drops, your nurse may give you a specific
                            amount of medical-grade saline through the machine lines to help stabilize you.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Why do my legs
                            cramp toward the end of dialysis?</h4>
                          <p style="margin:0; font-size:15px;">Leg cramps are usually a sign that your body is having
                            trouble adjusting to the fluid removal. It often happens as you move closer to your "dry
                            weight." Inform your nurse immediately so they can adjust the machine settings.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is a BP drop
                            dangerous?</h4>
                          <p style="margin:0; font-size:15px;">A mild drop can be managed easily. However, frequent,
                            severe drops put stress on the heart and can lead to "stunning"—a temporary reduction in
                            blood flow to the heart and brain. This is why we prioritize prevention through careful
                            monitoring.</p>
                        </div>
