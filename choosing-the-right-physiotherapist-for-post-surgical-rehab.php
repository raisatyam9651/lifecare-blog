                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        When should I start rehab after surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">In many cases, rehab starts
                                                        within 24 hours of surgery (even in the hospital bed!). The real
                                                        "outpatient" rehab typically begins 1 to 2 weeks after you are
                                                        discharged, once your surgical incisions have healed enough.
                                                        Always follow your surgeon's specific timeline.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I do my rehab at home?</h4>
                                                    <p style="margin:0; font-size:15px;">While you *must* do exercises
                                                        at home, it is not a substitute for professional clinic visits.
                                                        A therapist needs to manually check your joint movement, monitor
                                                        for signs of infection (like late-onset DVT), and adjust the
                                                        intensity of exercises based on your physical progress.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a "Physical Therapist" the same as a "Physiotherapist"?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Both terms refer to
                                                        the same medically trained professional. In Kenya and much of
                                                        the Commonwealth, "Physiotherapist" is the standard term, while
                                                        "Physical Therapist" is more common in the United States.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What should I wear to my first appointment?</h4>
                                                    <p style="margin:0; font-size:15px;">Wear comfortable, loose-fitting
                                                        clothing that allows easy access to the surgical area. For knee
                                                        or hip surgery, shorts are ideal. For shoulder surgery, a loose
                                                        tank top or t-shirt works best. Don't forget to bring
                                                        comfortable walking shoes.</p>
                                                </div>
