                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will physiotherapy make my pain worse before it gets better?
                                                    </h4>
                                                    <p style="margin:0; font-size:15px;">It's common to feel some muscle
                                                        soreness after starting a new exercise routine, similar to "gym
                                                        soreness." However, our therapists are trained to work within
                                                        your "pain window," ensuring that we challenge your body without
                                                        causing a major flare-up.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How many sessions will I need?</h4>
                                                    <p style="margin:0; font-size:15px;">Chronic pain management is a
                                                        marathon, not a sprint. While you may feel some relief in 2-3
                                                        sessions, most meaningful changes in muscle strength and nerve
                                                        sensitivity take 6 to 12 weeks of consistent work.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is massage the same as physiotherapy?</h4>
                                                    <p style="margin:0; font-size:15px;">No. While massage focuses on
                                                        immediate relaxation and soft-tissue comfort, physiotherapy is a
                                                        clinical diagnostic process that includes corrective exercise,
                                                        biomechanical retraining, and neurological education for
                                                        long-term functional improvement.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can physiotherapy help with fibromyalgia?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, absolutely. For
                                                        fibromyalgia, we focus on very gentle aerobic conditioning and
                                                        "pacing" strategies to help regulate the nervous system's
                                                        sensitivity to pain signals.</p>
