                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I drink traditional "Stinging Nettle" or herbal teas?</h4>
                                                    <p style="margin:0; font-size:15px;">**Be very careful.** Many
                                                        herbal teas are extremely high in potassium and can be dangerous
                                                        for dialysis patients. Always consult your Nephrologist at
                                                        LifeCare before starting any herbal "cleanses" or remedies.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is "Low-Sodium Salt" safe for me?</h4>
                                                    <p style="margin:0; font-size:15px;">**No!** Most "low-sodium" salts
                                                        found in shops replace sodium with **Potassium Chloride**. For a
                                                        dialysis patient, this is even more dangerous than regular salt.
                                                        Stick to fresh herbs and spices.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a dialysis patient eat fruit?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes**, but you must choose
                                                        carefully. Avoid the "Kravitz" fruits (high potassium) like
                                                        bananas and mangos. Choose "low-potassium" fruits like apples,
                                                        watermelon, and pineapple, and always keep an eye on the portion
                                                        size.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        I feel hungry all the time. What can I snack on?</h4>
                                                    <p style="margin:0; font-size:15px;">Try unsalted popcorn, plain
                                                        crackers with a little honey, or a few slices of apple. If you
                                                        find yourself constantly hungry, your protein levels might be
                                                        too low—speak to a LifeCare nutritionist to adjust your meal
                                                        plan.</p>
