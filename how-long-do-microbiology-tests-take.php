                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can antibiotics I've already taken affect the test?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, significantly. If you have
                                                        already started antibiotics, the bacteria in your sample might
                                                        not grow in the lab, leading to a "false negative." Always tell
                                                        your doctor if you've already taken any medication.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is a "Culture and Sensitivity" test?</h4>
                                                    <p style="margin:0; font-size:15px;">"Culture" is the process of
                                                        growing the bacteria. "Sensitivity" is the process of testing
                                                        which antibiotics kill that specific bacteria. They are almost
                                                        always done together.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Why did my report say "Commensal Flora"?</h4>
                                                    <p style="margin:0; font-size:15px;">"Commensal flora" are healthy,
                                                        normal bacteria that live on our skin or in our gut. It means
                                                        the lab didn't find any "bad" germs that cause disease, just the
                                                        normal ones that are supposed to be there.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a lab lose my sample?</h4>
                                                    <p style="margin:0; font-size:15px;">At LifeCare, we use barcoded
                                                        tracking for every sample. From the moment it leaves your side
                                                        to the moment the report is filed, we know exactly where it is.
                                                        Sample loss is extremely rare in a modern, automated system.</p>
                                                </div>
