                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does GDM mean I must have a C-section?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** If your blood sugar is
                                                        well-controlled and the baby is not excessively large, a normal
                                                        vaginal delivery is very possible. However, we monitor the
                                                        baby's size closely to make the safest decision for delivery.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
