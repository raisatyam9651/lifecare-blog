                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I manage GDM without insulin shots?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes, in many cases.** About
                                                        70-80% of women with gestational diabetes can manage the
                                                        condition through diet and exercise alone. Insulin is only used
                                                        if these natural methods fail to keep the sugar levels within a
                                                        safe range.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will my baby be born with diabetes?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Your baby will not have
                                                        diabetes at birth. However, they are at a higher risk for
                                                        childhood obesity and developing Type 2 Diabetes later in life.
                                                        A healthy upbringing and good nutrition can mitigate this risk.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I stop eating all sugar?</h4>
                                                    <p style="margin:0; font-size:15px;">You should limit "simple"
                                                        sugars (soda, sweets, white bread). However, your body and baby
                                                        still need carbohydrates for energy. The goal is to eat
                                                        "complex" carbohydrates (whole grains, sweet potatoes) that
                                                        digest slowly and don't cause spikes.</p>
                                                </div>
