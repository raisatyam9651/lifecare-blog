                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can a head CT
                            affect my fertility?</h4>
                          <p style="margin:0; font-size:15px;">No. The X-ray beam for a head CT is focused hundreds of
                            centimeters away from your reproductive organs. There is no evidence suggesting any impact
                            on sperm from scans performed above the waist.</p>
                        </div>
                        <div class="faq-item"
