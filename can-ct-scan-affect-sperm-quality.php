                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Will a CT scan
                            cause birth defects?</h4>
                          <p style="margin:0; font-size:15px;">There is currently no scientific data linking diagnostic
                            paternal radiation exposure (CT scans) to an increase in congenital birth defects. If sperm
                            is healthy enough to achieve fertilization, it is generally considered safe.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
