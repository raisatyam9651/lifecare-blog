                            safer than a CT?</h4>
                          <p style="margin:0; font-size:15px;">A Chest X-ray uses significantly less radiation than a CT
                            scan. However, a CT scan provides much more detailed information. Your doctor chooses the
                            test based on the depth of information needed, not just the radiation dose.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Does lead
                            shielding really work?</h4>
                          <p style="margin:0; font-size:15px;">Yes. Lead is extremely dense and acts as a physical
                            barrier that stops X-ray photons from passing through. Lead aprons and thyroid shields are
                            highly effective at protecting vital organs during diagnostic imaging.</p>
