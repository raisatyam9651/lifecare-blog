                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is mouthwash a substitute for flossing?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** While mouthwash can
                                                        kill surface bacteria and freshen breath, it cannot remove the
                                                        physical "biofilm" (plaque) that gets stuck between teeth. Only
                                                        flossing or interdental brushes can mechanically remove that
                                                        buildup.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a tooth infection cause a fever?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** A dental abscess (a
                                                        pocket of pus caused by a bacterial infection) can cause a high
                                                        fever, facial swelling, and a general feeling of being unwell
                                                        (malaise). This is a medical emergency that needs immediate
                                                        treatment at a hospital.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I change my toothbrush?</h4>
                                                    <p style="margin:0; font-size:15px;">You should replace your
                                                        toothbrush every 3 to 4 months, or sooner if the bristles are
                                                        frayed. A worn-out toothbrush is less effective at removing
                                                        plaque and can harbor its own bacteria over time.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is electric better than manual?</h4>
                                                    <p style="margin:0; font-size:15px;">Both can be effective if used
                                                        correctly. However, many people find that electric toothbrushes
                                                        make it easier to reach difficult spots and ensure they brush
                                                        for the full two minutes recommended by dentists.</p>
                                                </div>
