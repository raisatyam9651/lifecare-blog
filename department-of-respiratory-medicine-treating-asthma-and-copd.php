                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can kids outgrow asthma?</h4>
                                                    <p style="margin:0; font-size:15px;">Many children do see a
                                                        significant reduction in symptoms as they reach adolescence and
                                                        their airways grow larger. However, the underlying tendency for
                                                        "reactive airways" often remains, and asthma can resurface later
                                                        in life due to environmental triggers or smoking.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is the difference between asthma and COPD?</h4>
                                                    <p style="margin:0; font-size:15px;">Asthma is often triggered by
                                                        allergies and the narrowing of airways is usually **reversible**
                                                        with medication. COPD is a permanent, progressive damage to the
                                                        lung tissue and is usually the result of long-term exposure to
                                                        smoke or pollutants. However, some patients have an "overlap" of
                                                        both conditions.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does using a nebulizer at home replace a hospital visit?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** A home nebulizer is for
                                                        managing mild to moderate symptoms. If you find that the
                                                        nebulizer is not providing relief, or if your fingernails or
                                                        lips are turning blue, it is a life-threatening emergency that
                                                        requires immediate hospital intervention.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can diet affect my breathing?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Being overweight puts
                                                        pressure on your diaphragm and lungs, making breathing harder.
                                                        Some patients also find that high-sodium diets lead to fluid
                                                        retention, which can aggravate heart and lung issues. A lean,
                                                        anti-inflammatory diet overall supports lung function.</p>
                                                </div>
