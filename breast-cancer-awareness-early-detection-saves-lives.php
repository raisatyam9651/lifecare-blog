                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does injury to the breast cause cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** A hit or injury to the
                                                        breast does not cause cancer. However, an injury might cause you
                                                        to feel your breast more carefully, leading you to find a lump
                                                        that was already there. If you find a lump after an injury,
                                                        still get it checked.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        I'm under 40. Should I get a mammogram?</h4>
                                                    <p style="margin:0; font-size:15px;">Usually, no. For younger women,
                                                        the breast tissue is very "dense," which can make mammogram
                                                        results hard to read. In younger women with a concern, we
                                                        typically start with a breast Ultrasound, which is better at
                                                        seeing through dense tissue.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can men get breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** While much rarer than
                                                        in women (about 1% of cases), men do have breast tissue and can
                                                        develop breast cancer. Men should also report any unusual lumps
                                                        or nipple changes to a doctor immediately.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a mammogram painful?</h4>
                                                    <p style="margin:0; font-size:15px;">It involves "compression"
                                                        (flattening the breast for a few seconds) which can be
                                                        uncomfortable or slightly painful, but it is necessary for a
                                                        clear image. Choosing the right time (not right before your
                                                        period when breasts are tender) can help reduce discomfort.</p>
                                                </div>
