                                                    <p style="margin:0; font-size:15px;">Caffeine can cause a
                                                        short-lived spike in BP, even in people who don’t have
                                                        hypertension. For most people, moderate consumption is fine, but
                                                        if you have high BP, check with your doctor if you should limit
                                                        it.</p>
