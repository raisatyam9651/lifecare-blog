                        Frequently Asked Questions (FAQs)</h3>
                      <div class="faq-section" style="margin-bottom:20px;">
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can I give my
                            child medicine for an upset stomach?</h4>
                          <p style="margin:0; font-size:15px;">We generally advise against this unless prescribed. Many
                            "stomach settlers" for adults are dangerous for children. The best treatment for an upset
                            stomach is usually clear fluids and small, bland meals like bananas or rice.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">My child spat out
                            the medicine. Do I give it again?</h4>
                          <p style="margin:0; font-size:15px;">If the child vomits or spits up immediately (within 10-15
                            minutes), you can usually repeat the dose once. If it's been more than 20 minutes, do not
                            repeat it, as some of the medicine has already been absorbed.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can I mix medicine
                            with my baby's milk bottle?</h4>
                          <p style="margin:0; font-size:15px;">**Avoid doing this.** If the baby doesn't finish the
                            whole bottle, you won't know how much medicine they actually received. It's better to give
                            the medicine directly with a syringe, followed by the bottle.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can I use adult
                            medicine but give a smaller amount?</h4>
                          <p style="margin:0; font-size:15px;">**Never.** Adult tablets and liquids have much higher
                            concentrations of active ingredients. Even a "small" amount of an adult pill can be a
                            massive overdose for a child. Always use pediatric-specific formulations.</p>
                        </div>
