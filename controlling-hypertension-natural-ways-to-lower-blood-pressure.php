                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is a "normal" blood pressure reading?</h4>
                                                    <p style="margin:0; font-size:15px;">A "normal" reading is generally
                                                        considered to be below **120/80 mmHg**. Readings between 120-129
                                                        systolic are "elevated," and anything above 130/80 is considered
                                                        hypertension.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can caffeine raise blood pressure?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes, caffeine can cause a
                                                        temporary, sharp spike in your blood pressure, even if you don't
                                                        have high blood pressure. If you have hypertension, it’s best to
                                                        limit your coffee and tea intake, especially before activities
                                                        or medical check-ups.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is pink Himalayan salt better than table salt?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Chemically, they are
                                                        both almost entirely sodium chloride. While pink salt has trace
                                                        minerals, it still raises blood pressure exactly the same way as
                                                        white table salt. The key is to reduce **all** salt intake.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I stop my meds if my BP is now normal?</h4>
                                                    <p style="margin:0; font-size:15px;">**Never** stop blood pressure
                                                        medication without your doctor's approval. Often, your blood
                                                        pressure is normal *because* the medication is working. We may
                                                        choose to taper your dose as your lifestyle improves, but this
                                                        must be done under strict medical supervision at LifeCare.</p>
