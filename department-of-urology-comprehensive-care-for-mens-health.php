                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        At what age should I start prostate screening?</h4>
                                                    <p style="margin:0; font-size:15px;">Most men should start regular
                                                        screenings at **age 50**. However, if you have a family history
                                                        of prostate cancer or are of African descent, we recommend
                                                        starting at **age 45** or even 40, as the risk is statistically
                                                        higher.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are kidney stones hereditary?</h4>
                                                    <p style="margin:0; font-size:15px;">There is a genetic component.
                                                        If your parents or siblings had stones, you are more likely to
                                                        have them. However, diet (high salt, high protein) and poor
                                                        hydration are much bigger factors that you can actually control.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does "BPH" enlargement mean I have cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Benign Prostatic
                                                        Hyperplasia is, by definition, benign (not cancerous). It is
                                                        extremely common as men age. However, the symptoms of BPH and
                                                        prostate cancer can be similar, which is why a professional
                                                        evaluation at LifeCare is necessary.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is Erectile Dysfunction always permanent?</h4>
                                                    <p style="margin:0; font-size:15px;">**Rarely.** Most cases of ED
                                                        are related to lifestyle (smoking, obesity), underlying disease
                                                        (diabetes), or psychological stress. With proper medical
                                                        management and lifestyle changes, most men find significant
                                                        improvement.</p>
                                                </div>
