                            better job prospects?</h4>
                          <p style="margin:0; font-size:15px;">Both. With an aging population and a rise in sedentary
                            lifestyles/sports injuries, both skilled medical doctors and physiotherapists are in
                            extremely high demand across Kenya and the East African region.</p>
