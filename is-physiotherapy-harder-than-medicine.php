                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">How many years
                            does it take to become a Physiotherapist in Kenya?</h4>
                          <p style="margin:0; font-size:15px;">Typically, a Bachelor's degree in Physiotherapy takes 4
                            years of academic study plus a 1-year clinical internship. Many then pursue specialized
                            Master's degrees in areas like sports or neuro-rehabilitation.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can a
                            Physiotherapist prescribe medication?</h4>
                          <p style="margin:0; font-size:15px;">In Kenya, the primary responsibility for prescribing
                            medication lies with medical doctors and clinical officers. Physiotherapists focus on
                            non-pharmacological treatments, though they work closely with doctors to manage a patient's
                            pain medication during rehab.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is a
                            Physiotherapist a doctor?</h4>
                          <p style="margin:0; font-size:15px;">In many parts of the world, including the US,
                            physiotherapists earn a "Doctor of Physical Therapy" (DPT) degree. In Kenya, they are
                            clinical specialists. While they may not use the title 'Doctor' in the traditional medical
                            sense, they are primary care practitioners in their field.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
