                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does compression increase radiation risk?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** In fact, compression is
                                                        a safety feature! By flattening the breast tissue, the X-rays
                                                        have less tissue to penetrate, which actually **lowers** the
                                                        required radiation dose and makes the image clearer.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a breast ultrasound better than a mammogram?</h4>
                                                    <p style="margin:0; font-size:15px;">Ultrasound has zero radiation,
                                                        but it is **not** a replacement for mammography. Ultrasound
                                                        cannot see "micro-calcifications," which are often the very
                                                        first signs of breast cancer. We usually use them together for a
                                                        "Gold Standard" evaluation.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does a mammogram cause the cancer to spread?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** This is a dangerous
                                                        myth. The compression and the low-dose X-rays have no physical
                                                        or biological ability to cause a tumor to "release" cells or
                                                        move to other parts of the body.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I avoid deodorant for my mammogram?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Many deodorants and
                                                        powders contain tiny metallic particles (like aluminum) that can
                                                        show up on the X-ray as "white spots." These can be mistaken for
                                                        calcifications, leading to unnecessary worry or repeat scans.
                                                        Wash your underarms before the appointment and apply deodorant
                                                        *after*.</p>
                                                </div>
