                                                        How long does treatment usually last?</h4>
                                                    <p style="margin:0; font-size:15px;">Each case is different. Surgery
                                                        may take a few hours with a few weeks of recovery. Chemotherapy
                                                        can last for 3 to 6 months. Radiation therapy often requires
