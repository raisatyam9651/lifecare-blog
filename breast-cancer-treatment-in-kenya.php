                                                        Will I lose my hair during chemotherapy?</h4>
                                                    <p style="margin:0; font-size:15px;">It depends on the specific
                                                        chemotherapy drugs used. Some cause total hair loss, while
                                                        others cause thinning. If hair loss occurs, it is almost always
                                                        temporary—hair typically begins to grow back a few weeks after
                                                        the final treatment session.</p>
