                                            <h3
                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can stress cause temporary hypertension?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Stress triggers a
                                                        surge of hormones that temporarily increases your heart rate and
                                                        cause your blood vessels to narrow. However, once the stressor
                                                        is gone, your BP should return to normal. If it stays high, you
                                                        may have chronic hypertension.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is "White Coat Hypertension"?</h4>
                                                    <p style="margin:0; font-size:15px;">This is a condition where a
                                                        patient's blood pressure is high in a hospital or clinic setting
                                                        because they are anxious about seeing a doctor (the "White
                                                        Coat"), but normal when they are at home. We often use 24-hour
                                                        monitoring to confirm this.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is there a cure for hypertension?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Once you are diagnosed
                                                        with chronic hypertension, it is generally a lifelong condition
                                                        that must be **managed**. Many people can lower their BP to
                                                        normal levels through lifestyle changes, but if they return to
                                                        old habits, the pressure will rise again.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can salt really affect me if I don't feel sick?</h4>
                                                    <p style="margin:0; font-size:15px;">**Absolutely.** Salt (sodium)
                                                        causes your body to hold onto water. The extra water in your
                                                        blood increases the volume and pressure against your vessel
                                                        walls. Even if you don't "feel" it, the damage is happening
                                                        silently every day.</p>
                                                </div>
