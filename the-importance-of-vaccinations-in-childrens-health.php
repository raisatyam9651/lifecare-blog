                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a child get vaccinated if they have a mild cold?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** A mild runny nose or
                                                        slight cough is usually not a reason to delay vaccination.
                                                        However, if your child has a high fever (above 38.5°C) or a
                                                        severe illness, our pediatrician may recommend waiting a few
                                                        days.</p>
