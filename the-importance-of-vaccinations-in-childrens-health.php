                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How can I help my child during the injection?</h4>
                                                    <p style="margin:0; font-size:15px;">Remain calm—children take their
                                                        emotional cues from you. For babies, breastfeeding or a pacifier
                                                        during the shot can help. For older children, bring their
                                                        favorite toy and avoid using the vaccine as a "threat" ("If
                                                        you're bad, the doctor will give you an injection").</p>
