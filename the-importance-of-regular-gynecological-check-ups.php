                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        At what age should a girl first see a gynecologist?</h4>
                                                    <p style="margin:0; font-size:15px;">The American College of
                                                        Obstetricians and Gynecologists recommends the first visit
                                                        between ages 13 and 15. This first visit is usually just a talk
                                                        to build a relationship and discuss normal development, not a
                                                        pelvic exam.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often do I really need a Pap smear?</h4>
                                                    <p style="margin:0; font-size:15px;">Guidelines have changed. Most
                                                        women aged 21 to 29 should have a Pap smear every 3 years. Women
                                                        aged 30 to 65 can have a Pap and HPV test every 5 years
                                                        (co-testing). However, even if you don't need a Pap smear every
                                                        year, you still need a **Well-Woman physical exam** every year.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Should I reschedule my visit if I have my period?</h4>
                                                    <p style="margin:0; font-size:15px;">If you are coming for a
                                                        specific problem like abnormal bleeding, it might be helpful for
                                                        us to see you while you are bleeding. However, for a routine Pap
                                                        smear, it is best to be in the middle of your cycle (neither
                                                        bleeding nor very near it) for the most accurate cell samples.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is a gynecologist the same as a primary care doctor?</h4>
                                                    <p style="margin:0; font-size:15px;">They specialize in reproductive
                                                        health. While they can diagnose high blood pressure or basic
                                                        issues, you still need a general GP for things like respiratory
                                                        infections, gastrointestinal issues, or complex metabolic
                                                        management. Ideally, your OBGYN and GP should coordinate through
                                                        our shared records at LifeCare.</p>
                                                </div>
