                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I really see a dentist?</h4>
                                                    <p style="margin:0; font-size:15px;">For most adults, every **6
                                                        months** is the ideal. This allows us to catch tiny cavities
                                                        before they become painful (and expensive) root canals. If you
                                                        have gum disease, we may suggest every 3-4 months.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        My gums bleed when I brush. Is this normal?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Bleeding gums are a
                                                        clear sign of gingivitis (early gum disease). It means plaque is
                                                        irritating your gums. A professional scaling and better flossing
                                                        habits can usually reverse this completely.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is teeth whitening safe for enamel?</h4>
                                                    <p style="margin:0; font-size:15px;">When performed by a
                                                        professional at LifeCare, **yes.** We use balanced gels that
                                                        brighten your teeth without damaging the structure. You might
                                                        have some temporary sensitivity to cold for 24 hours, but your
                                                        enamel remains safe.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        When should my child first visit a dentist?</h4>
                                                    <p style="margin:0; font-size:15px;">As soon as their first tooth
                                                        appears, or by their **first birthday.** These early visits are
                                                        about making the child "comfortable" with the dentist and
                                                        checking for any developmental issues early on.</p>
