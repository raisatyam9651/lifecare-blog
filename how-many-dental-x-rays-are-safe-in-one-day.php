        Frequently Asked Questions (FAQs)</h3>
      <div class="faq-section" style="margin-bottom:20px;">
        <div class="faq-item"
          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can X-rays cause
            cancer?</h4>
          <p style="margin:0; font-size:15px;">Very high doses of radiation can increase cancer risk.
            However, the dose in a dental X-ray is so extremely low (0.005 mSv) that there is no
            clinical evidence of it causing cancer. You receive more radiation from your granite
            countertops or a day at the beach than from a dental visit.</p>
        </div>
        <div class="faq-item"
          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">How often should I
            have dental X-rays?</h4>
          <p style="margin:0; font-size:15px;">For a healthy adult with good oral hygiene, every 12 to
            24 months is standard. If you have a history of frequent cavities or gum disease, your
            dentist may recommend them every 6 months until the issue is stabilized.</p>
        </div>
        <div class="faq-item"
          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is a Panoramic
            X-ray better than a bite-wing?</h4>
          <p style="margin:0; font-size:15px;">Neither is "better"—they do different things. Bite-wings
            are perfect for finding cavities between teeth. A panoramic scan is better for looking at
            wisdom teeth, jaw joints, and the overall structure of the mouth. Often, your dentist will
            need both for a complete picture.</p>
        </div>
        <div class="faq-item"
          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Do dental X-rays
            affect dental implants?</h4>
          <p style="margin:0; font-size:15px;">No. Radiation at diagnostic levels has no effect on
            titanium implants or porcelain crowns. In fact, X-rays are essential to monitor the health
            of the bone supporting your implants.</p>
        </div>
