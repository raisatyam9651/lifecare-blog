                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does drinking milk as an adult really help?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** While many adults in
                                                        Kenya become lactose intolerant, dairy remains one of the most
                                                        bioavailable (easy to absorb) sources of calcium. If you can't
                                                        tolerate milk, try yogurt, cheese, or lactose-free alternatives
                                                        fortified with calcium.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can men get osteoporosis?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** While it is more
                                                        common in women due to hormonal changes, men are absolutely at
                                                        risk, especially if they smoke, have low testosterone, or take
                                                        certain medications like steroids for asthma or arthritis.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is joint pain a sign of weak bones?</h4>
                                                    <p style="margin:0; font-size:15px;">Not directly. Joint pain is
                                                        usually Articular (related to cartilage and arthritis).
                                                        Osteoporosis itself doesn't cause pain until a bone actually
                                                        breaks. However, the two conditions often happen together, so a
                                                        combined orthopedic assessment at LifeCare is best.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How much sun do I need for Vitamin D?</h4>
                                                    <p style="margin:0; font-size:15px;">For most people in Kenya, 10 to
                                                        20 minutes of direct sun on the face, arms, and legs 3 times a
                                                        week is enough to maintain levels. However, if you are strictly
                                                        covered for religious or cultural reasons, you almost certainly
                                                        need a supplement.</p>
