                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a slipped disc heal without surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Approximately 90% of
                                                        slipped discs (herniations) stabilize and the pain resolves with
                                                        conservative care (physiotherapy) within 6 to 12 weeks. Surgery
                                                        is typically only considered if there is severe nerve damage or
                                                        if conservative care fails.</p>
