                                                        Will physio make my pain worse before it gets better?</h4>
                                                    <p style="margin:0; font-size:15px;">It is common to feel some
                                                        muscle soreness (similar to after a workout) as you begin to
                                                        move stiff joints and dormant muscles. However, acute, sharp
                                                        pain is not normal. Always communicate clearly with your
                                                        therapist about how you feel during each exercise.</p>
