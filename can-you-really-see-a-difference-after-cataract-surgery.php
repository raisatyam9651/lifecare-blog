                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can cataracts grow back?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Once the natural lens
                                                        is removed and replaced by an artificial one, a cataract cannot
                                                        re-grow. Some patients develop a "secondary cataract" (a
                                                        clouding of the membrane that holds the new lens), but this is
                                                        easily fixed with a 2-minute laser procedure called a YAG
                                                        capsulotomy.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does the surgery hurt?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** We use anesthetic eye
                                                        drops to completely numb the eye. You may feel a slight
                                                        sensation of pressure, but no pain. We also offer light sedation
                                                        to help you remain relaxed throughout the procedure.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long does the new lens last?</h4>
                                                    <p style="margin:0; font-size:15px;">The artificial lens (IOL) is
                                                        designed to last for your entire lifetime. It does not wear out
                                                        or require replacement. It is made of highly biocompatible
                                                        materials that your body accepts permanently.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can both eyes be done at the same time?</h4>
                                                    <p style="margin:0; font-size:15px;">Usually, no. Most surgeons
                                                        prefer to do one eye first and wait a week or two for it to heal
                                                        before performing surgery on the second eye. This ensures you
                                                        always have one "good" eye to use while the first one is
                                                        recovering.</p>
                                                </div>
