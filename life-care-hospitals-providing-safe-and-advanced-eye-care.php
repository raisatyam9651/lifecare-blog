                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I have an eye exam?</h4>
                                                    <p style="margin:0; font-size:15px;">Healthy adults with no symptoms
                                                        should have a comprehensive exam every 2 years. If you are over
                                                        60, diabetic, or have a family history of glaucoma, you should
                                                        visit us at least **once a year**.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can you cure "Lazy Eye" in adults?</h4>
                                                    <p style="margin:0; font-size:15px;">It is much easier to treat in
                                                        early childhood (under age 7), but modern "vision therapy" and
                                                        specialized treatments can often provide significant
                                                        improvements even for adults. It is always worth an assessment.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What are "floaters" and are they dangerous?</h4>
                                                    <p style="margin:0; font-size:15px;">Floaters are small shapes that
                                                        move across your field of vision. Most are harmless. However, if
                                                        you suddenly see a **large increase** in floaters or see
                                                        "flashes of light," seek emergency eye care immediately—this can
                                                        be a sign of a retinal detachment.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does screen time really damage eyes?</h4>
                                                    <p style="margin:0; font-size:15px;">Screens cause "Digital Eye
                                                        Strain" (dryness, headache, and blurry vision), but there is no
                                                        proof they cause permanent structural damage. However, for
                                                        children, excessive near-work (like screens) is linked to
                                                        developing nearsightedness (Myopia). We recommend the **20-20-20
                                                        rule**.</p>
                                                </div>
