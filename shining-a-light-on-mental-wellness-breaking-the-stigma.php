                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are antidepressants "addictive"?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Antidepressants are not
                                                        drugs of abuse. They don't give you a "high" or make you feel
                                                        euphoric. They simply help normalize the chemical levels in your
                                                        brain so you can function again. While you may need to taper off
                                                        them slowly under a doctor's guidance, you do not become
                                                        "addicted" to them.</p>
                                                </div>
                                                <div class="faq-item"
