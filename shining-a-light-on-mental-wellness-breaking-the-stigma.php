                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        I feel embarrassed to see a psychiatrist. What if people see me?
                                                    </h4>
                                                    <p style="margin:0; font-size:15px;">At LifeCare, our psychiatry
                                                        clinics are integrated within our general hospital setting.
                                                        Walking into our lobby could mean you are seeing an eye doctor,
                                                        a dentist, or a psychiatrist. Your privacy is our priority, and
                                                        the "embarrassment" is actually part of the stigma we are trying
                                                        to break.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is the difference between a Psychologist and a
                                                        Psychiatrist?</h4>
                                                    <p style="margin:0; font-size:15px;">A **Psychologist** focuses on
                                                        "Talk Therapy" and behavioral strategies. A **Psychiatrist** is
                                                        a medical doctor who can perform physical evaluations and
                                                        prescribe medications. At LifeCare, these two often work
                                                        together as a team to give you the best results.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can kids have mental health issues?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** Children can
                                                        experience anxiety, depression, and ADHD. Their symptoms often
                                                        look different than adults (e.g., changes in school performance
                                                        or extreme acting out). Early intervention is key to a child's
                                                        future success.</p>
