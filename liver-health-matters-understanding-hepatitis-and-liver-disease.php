                                                        Is a liver biopsy painful?</h4>
                                                    <p style="margin:0; font-size:15px;">A biopsy involves a needle to
                                                        take a tiny sample. We use local anesthesia to numb the area, so
                                                        it feels like a "pressure" or a quick pinch. However, with new
                                                        technologies like **FibroScan**, many patients no longer need a
                                                        biopsy to check for scarring.</p>
