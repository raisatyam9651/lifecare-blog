                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does Panadol (Paracetamol) damage the liver?</h4>
                                                    <p style="margin:0; font-size:15px;">**Only in excess.** Paracetamol
                                                        is safe in normal doses. However, taking more than the
                                                        recommended amount, or taking it with heavy alcohol, is the
                                                        leading cause of acute liver failure. Always follow the dosage
                                                        on the packet.</p>
