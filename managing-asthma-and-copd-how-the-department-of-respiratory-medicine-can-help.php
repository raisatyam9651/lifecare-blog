                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a "non-smoker" get COPD?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** In Kenya, a
                                                        significant number of COPD cases in rural areas are caused by
                                                        years of indoor cooking with wood or charcoal. Secondhand smoke
                                                        and industrial air pollution are also risk factors.</p>
                                                </div>
                                                <div class="faq-item"
