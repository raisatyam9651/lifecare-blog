                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is Asthma curable?</h4>
                                                    <p style="margin:0; font-size:15px;">**No, but it is 100%
                                                        controllable.** With the right preventer medication, most asthma
                                                        patients can live lives with NO symptoms, NO nighttime wake-ups,
                                                        and the ability to exercise normally. Many children "grow out"
                                                        of asthma as their lungs mature, but for adults, it is a
                                                        life-long management journey.</p>
