                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is it safe to exercise with asthma?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes!** In fact, exercise is
                                                        good for your lungs. If your asthma is well-controlled, you
                                                        should be able to do any sport. If you have "exercise-induced"
                                                        asthma, using your inhaler 15 minutes before you start typically
                                                        prevents any symptoms.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are inhalers addictive?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** This is a dangerous
                                                        common myth. Inhalers are not addictive. If you find yourself
                                                        needing your inhaler more often, it doesn't mean you are
                                                        "addicted"—it means your underlying lung disease is getting
                                                        worse or is not well-controlled, and you need to see your
                                                        LifeCare doctor to adjust your plan.</p>
