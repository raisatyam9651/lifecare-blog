                        Frequently Asked Questions (FAQs)</h3>
                      <div class="faq-section" style="margin-bottom:20px;">
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is drinking lots
                            of water always good for the kidneys?</h4>
                          <p style="margin:0; font-size:15px;">For healthy kidneys, yes—it helps flush toxins. However,
                            for patients with advanced kidney failure (Stage 4 or 5), the kidneys lose the ability to
                            remove fluid. In these cases, doctors often prescribe a "fluid restriction." Always check
                            with your doctor about your specific fluid needs.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Are bananas bad
                            for my kidneys?</h4>
                          <p style="margin:0; font-size:15px;">Bananas are healthy but VERY high in potassium. If your
                            kidneys are healthy, they are fine. if you have chronic kidney disease, you should limit
                            bananas and choose lower-potassium fruits like apples, berries, or pineapples instead.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can I eat salt
                            substitutes?</h4>
                          <p style="margin:0; font-size:15px;">**Be careful.** Most salt substitutes (like "Lite Salt")
                            replace sodium with **potassium chloride**. For someone with kidney issues, this "extra"
                            potassium can be dangerous for the heart. Use herbs and spices like garlic, lemon, and
                            pepper instead.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is spinach good
                            for kidney stones?</h4>
                          <p style="margin:0; font-size:15px;">If you have a history of **calcium-oxalate kidney
                            stones**, you should limit high-oxalate foods like spinach and beets. Talk to our urologists
                            at LifeCare to find out which type of stone you have and the specific diet to prevent it.
                          </p>
