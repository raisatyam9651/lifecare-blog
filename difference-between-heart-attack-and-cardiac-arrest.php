                                            <h1>Heart Attack vs. Cardiac Arrest: Key Differences</h1>

                                            <p>People often use the terms "heart attack" and "cardiac arrest" interchangeably, but they are not the same thing. Understanding the difference can be life-saving. At <strong>LifeCare Hospitals</strong>, our cardiology and emergency teams are trained to respond rapidly to both, but the first response you provide at home or in public depends on knowing what is happening. In short: a heart attack is a <strong>circulation</strong> problem, while cardiac arrest is an <strong>electrical</strong> problem.</p>

                                            <h2>What is a Heart Attack?</h2>
                                            <p>A heart attack (myocardial infarction) occurs when blood flow to the heart is blocked. If the blocked artery is not reopened quickly, the portion of the heart normally nourished by that artery begins to die.</p>
                                            <ul>
                                                <li><strong>Symptoms:</strong> Chest pain or pressure, radiating pain to the arm, back, or jaw, shortness of breath, and cold sweats. (Read more on <a href="/blog/acidity-or-heart-attack-how-to-tell-the-difference">Acidity vs. Heart Attack</a>).</li>
                                                <li><strong>Consciousness:</strong> The person is usually conscious during a heart attack.</li>
                                                <li><strong>Risk Factors:</strong> High blood pressure, high cholesterol, and <a href="/blog/abc-of-diabetes">Diabetes</a>.</li>
                                            </ul>

                                            <h2>What is Cardiac Arrest?</h2>
                                            <p>Cardiac arrest occurs suddenly and often without warning. It is caused by an electrical malfunction in the heart that causes an irregular heartbeat (arrhythmia), leading the heart to stop pumping blood to the brain and lungs.</p>
                                            <ul>
                                                <li><strong>Symptoms:</strong> Sudden collapse, loss of consciousness, and no breathing or pulse.</li>
                                                <li><strong>Consciousness:</strong> The person becomes unconscious immediately.</li>
                                                <li><strong>Immediate Action:</strong> Requires CPR (Cardiopulmonary Resuscitation) and the use of a defibrillator (AED) immediately.</li>
                                            </ul>

