                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What is the most common workplace injury in Kenya?</h4>
                                                    <p style="margin:0; font-size:15px;">Musculoskeletal disorders (back
                                                        and neck pain) are the most frequent, often caused by poor
                                                        posture or improper lifting. Slips, trips, and falls are also
                                                        highly common in the industrial and agricultural sectors.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does my employer have to pay for my health check-up?</h4>
                                                    <p style="margin:0; font-size:15px;">Workplaces that involve
                                                        high-risk activities (hazardous chemicals, extreme noise) are
                                                        legally required under the OSHA (Occupational Safety and Health
                                                        Act) 2007 to provide medical examinations for their staff. Many
                                                        other employers provide these as part of their HR benefits.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How can I stop my eyes from hurting at my computer?</h4>
                                                    <p style="margin:0; font-size:15px;">Besides the 20-20-20 rule,
                                                        ensure your screen lighting matches the room lighting. Avoid
                                                        glare from windows, and consider "blue light" filters for your
                                                        glasses or screen. Also, remember to **blink**—we blink 66% less
                                                        when looking at screens, which dries the eyes out.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Where do I go if I am injured at work?</h4>
                                                    <p style="margin:0; font-size:15px;">Report it to your supervisor
                                                        immediately to ensure it is documented for insurance/WIBA (Work
                                                        Injury Benefits Act) purposes. Then, proceed to the nearest
                                                        LifeCare Hospital emergency room for assessment. We handle all
                                                        trauma and work-related injuries with expert urgency.</p>
                                                </div>
