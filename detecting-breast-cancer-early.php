                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does a mammogram hurt?</h4>
                                                    <p style="margin:0; font-size:15px;">A mammogram involves briefly
                                                        compressing the breast between two plates. It can be
                                                        uncomfortable for a few seconds, but it is not "painful." To
                                                        minimize discomfort, we suggest scheduling your mammogram for
                                                        the week after your period when your breasts are less sensitive.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can men get breast cancer?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** While it is rare
                                                        (about 1% of cases), men do have breast tissue and can develop
                                                        breast cancer. Men should also pay attention to any hard lumps
                                                        or skin changes in the chest area and see a doctor immediately
                                                        if found.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        If my grandmother had it, will I?</h4>
                                                    <p style="margin:0; font-size:15px;">Family history increases your
                                                        risk, but most breast cancer cases (about 85%) occur in women
                                                        with **no** family history. This is why screening is vital for
                                                        *every* woman, regardless of her family tree.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is the radiation from a mammogram dangerous?</h4>
                                                    <p style="margin:0; font-size:15px;">The dose of radiation is
                                                        extremely small—roughly equivalent to the amount of natural
                                                        background radiation you would receive from just being alive for
                                                        7 weeks. The life-saving benefit far outweighs this tiny risk.
                                                    </p>
