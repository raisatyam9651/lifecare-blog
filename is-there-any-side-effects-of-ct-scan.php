                      <h3 style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                        Frequently Asked Questions (FAQs)</h3>
                      <div class="faq-section" style="margin-bottom:20px;">
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Does radiation
                            stay in my body after the scan?</h4>
                          <p style="margin:0; font-size:15px;">No. Radiation from a CT scan is like light from a
                            flashcube; once the scan is over, the radiation is gone. You are not "radioactive" and
                            cannot harm anyone else (including children or pregnant women) after your scan.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Can a CT scan make
                            my hair fall out?</h4>
                          <p style="margin:0; font-size:15px;">No. The doses used in diagnostic imaging are far too low
                            to cause hair loss. Hair loss is a potential side effect of high-dose radiation *therapy*
                            used to treat cancer, but not a diagnostic CT scan.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">How long does the
                            contrast dye take to leave my body?</h4>
                          <p style="margin:0; font-size:15px;">For most people with healthy kidneys, the dye is almost
                            entirely eliminated through urine within 24 hours. We recommend drinking 4 to 6 extra
                            glasses of water after your scan to help flush it out.</p>
                        </div>
                        <div class="faq-item"
                          style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                          <h4 style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">Is a CT safer than
                            an MRI?</h4>
                          <p style="margin:0; font-size:15px;">An MRI uses no radiation (it uses magnets), so in that
                            sense, it has fewer "radiation risks." However, MRIs are slower, more expensive, and cannot
                            be used for all diagnoses. Neither is "safer"—they are different tools for different jobs.
                          </p>
                        </div>
