                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does stress physically damage the brain?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes. Chronic stress causes
                                                        long-term elevation of cortisol, which can shrink the
                                                        "hippocampus"—the area of the brain responsible for memory and
                                                        emotional regulation.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a "brain fog" be a medical sign?</h4>
                                                    <p style="margin:0; font-size:15px;">Yes. Brain fog is not a
                                                        condition itself but a symptom of something else—possibly
                                                        hormonal imbalance, nutritional deficiencies, or thậm chí
                                                        thyroid issues. It should always be investigated.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are multiple MRIs dangerous for the brain?</h4>
                                                    <p style="margin:0; font-size:15px;">No. Unlike X-rays, MRIs use
                                                        magnetic fields and do not involve radiation. They are safe for
                                                        repeated use over a lifetime.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can epilepsy be cured?</h4>
                                                    <p style="margin:0; font-size:15px;">While many cases can't be
                                                        "cured" permanently, over 70% of people with epilepsy can become
                                                        seizure-free with the right medication. Some children also
                                                        outgrow certain forms of epilepsy as they age.</p>
                                                </div>
