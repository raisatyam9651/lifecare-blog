                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Do I really need to see a dentist if nothing hurts?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** By the time a tooth
                                                        hurts, the decay has usually reached the nerve. This means a
                                                        complex root canal or an extraction. Regular check-ups catch
                                                        issues in the "painless" stage when a small filling can fix the
                                                        problem for a fraction of the cost.</p>
                                                </div>
                                                <div class="faq-item"
