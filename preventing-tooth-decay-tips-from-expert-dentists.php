                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are electric toothbrushes better?</h4>
                                                    <p style="margin:0; font-size:15px;">Clinical studies show that
                                                        oscillating electric toothbrushes remove significantly more
                                                        plaque than manual ones. They are especially helpful for
                                                        children or adults who have difficulty with the "manual
                                                        dexterity" required for perfect brushing.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does whitening my teeth weaken the enamel?</h4>
                                                    <p style="margin:0; font-size:15px;">When performed professionally
                                                        by a LifeCare dentist, whitening is perfectly safe. "Black
                                                        market" or unregulated whitening kits can contain harmful acids
                                                        that permanently destroy enamel. Always stick to professional
                                                        clinical solutions.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Why do my gums bleed when I brush?</h4>
                                                    <p style="margin:0; font-size:15px;">Healthy gums Never bleed.
                                                        Bleeding is the first sign of Gingivitis (gum inflammation)
                                                        caused by plaque buildup. The answer is NOT to stop brushing,
                                                        but to brush more thoroughly and see a dentist for a
                                                        professional cleaning to remove the source of irritation.</p>
                                                </div>
