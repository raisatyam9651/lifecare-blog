                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long should my child fast before surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">Typically, no solid food for
                                                        6-8 hours before the procedure. For infants, we have specific
                                                        "clear fluid" guidelines (like breastmilk or apple juice) that
                                                        can be given up to 2-4 hours before. We will provide a strict
                                                        schedule during your pre-op visit.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will my child be in a lot of pain afterwards?</h4>
                                                    <p style="margin:0; font-size:15px;">We use "local blocks" along
                                                        with general anesthesia, meaning the surgical site stays numb
                                                        for several hours after the child wakes up. We then use a
                                                        rotation of child-safe pain relievers to keep them comfortable
                                                        at home.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        What happens if my child has a cold on the day of surgery?</h4>
                                                    <p style="margin:0; font-size:15px;">If the cold is mild (just a
                                                        clear runny nose), we may proceed. If there is a fever, a
                                                        productive cough, or wheezing, we will likely postpone the
                                                        surgery by 2 to 4 weeks for safety. Our anesthesiologist will
                                                        make the final call on the morning of the procedure.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How soon can my child return to school?</h4>
                                                    <p style="margin:0; font-size:15px;">For minor procedures
                                                        (tubes/circ), 2-3 days. For minor abdominal surgery
                                                        (hernia/appendectomy), usually 1 week. We will provide a
                                                        specific "Return to Activity" plan for your child's recovery.
                                                    </p>
                                                </div>
