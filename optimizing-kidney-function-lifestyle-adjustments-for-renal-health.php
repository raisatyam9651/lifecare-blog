                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does "Juice Cleansing" detox the kidneys?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Your kidneys ARE the
                                                        detox system. Juice cleanses can actually be harmful if they are
                                                        high in oxalates (found in spinach or beets), which can cause
                                                        kidney stones. The best way to "detox" is to drink clean water
                                                        and limit processed foods.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are kidney stones a sign of kidney disease?</h4>
                                                    <p style="margin:0; font-size:15px;">Not necessarily, but they are a
                                                        sign your kidneys are under stress. Repeated stones can cause
                                                        scarring and lead to CKD over time. At LifeCare, we treat the
                                                        stones and then analyze them to tell you exactly which foods to
                                                        avoid to prevent them from coming back.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can the kidneys regrow if they get damaged?</h4>
                                                    <p style="margin:0; font-size:15px;">In cases of **Acute Kidney
                                                        Injury** (temporary damage from a severe infection or
                                                        medication), they can often recover fully. However, **Chronic
                                                        Kidney Disease** (damage over years from BP or Diabetes)
                                                        involves permanent scarring that cannot be reversed. This is why
                                                        early detection is so vital.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I check my kidney function?</h4>
                                                    <p style="margin:0; font-size:15px;">If you are young and healthy,
                                                        once every 2-3 years during a wellness check is fine. If you
                                                        have Diabetes or Hypertension, you should have your blood
                                                        (Creatinine/eGFR) and urine checked **at least once a year**.
                                                    </p>
