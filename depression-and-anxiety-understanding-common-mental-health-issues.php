                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How is a panic attack different from a heart attack?</h4>
                                                    <p style="margin:0; font-size:15px;">They can feel terrifyingly
                                                        similar. Both involve chest pain, shortness of breath, and a
                                                        racing heart. If you are experiencing these symptoms for the
                                                        first time, **treat it as an emergency and visit our A&E.** Once
                                                        we have ruled out heart issues, we can help you manage the panic
                                                        disorder.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Are antidepressants addictive?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Antidepressants are not
                                                        addictive like painkillers or drugs of abuse. They do not give
                                                        you a "high." However, they must be stopped slowly under a
                                                        doctor's supervision to avoid "discontinuation syndrome"
                                                        (temporary withdrawal-like symptoms).</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How long do I need to stay on medication?</h4>
                                                    <p style="margin:0; font-size:15px;">It depends on the individual.
                                                        For a first episode of depression, a doctor may recommend 6 to
                                                        12 months. For someone with chronic, recurring issues,
                                                        longer-term maintenance may be best. The decision is always made
                                                        collaboratively between you and your psychiatrist.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Will therapy make me change my personality?</h4>
                                                    <p style="margin:0; font-size:15px;">**No.** Therapy is about
                                                        helping you become the best version of *yourself*. It doesn't
                                                        change who you are; it changes how you *react* to the world and
                                                        how you process your emotions.</p>
