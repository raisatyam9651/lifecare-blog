                                                style="color:#00b2e3; font-size:26px; font-weight:bold; margin-top:40px; margin-bottom:20px;">
                                                Frequently Asked Questions (FAQs)</h3>
                                            <div class="faq-section" style="margin-bottom:20px;">
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is "Red Meat" bad for the heart?</h4>
                                                    <p style="margin:0; font-size:15px;">**In moderation, it's okay.**
                                                        The problem is typically the high amount of saturated fat and
                                                        how it's prepared (frying in oil). Choose lean cuts and try to
                                                        have several "meat-free" days each week focusing on fish or
                                                        plant proteins like beans (*Maharagwe*).</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can exercise cause a heart attack?</h4>
                                                    <p style="margin:0; font-size:15px;">For a healthy heart, exercise
                                                        is the best defense. However, if you have a pre-existing (and
                                                        often unknown) heart condition, suddenly starting very intense
                                                        exercise can be risky. We always recommend a "Heart Screening"
                                                        before starting a new heavy athletic program if you are over 40.
                                                    </p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Does alcohol protect the heart?</h4>
                                                    <p style="margin:0; font-size:15px;">**This is a common
                                                        misconception.** While some studies suggest a very small benefit
                                                        for light drinkers, heavy alcohol use directly damages the heart
                                                        muscle (Cardiomyopathy) and raises blood pressure. It is never
                                                        recommended to "start" drinking for heart health.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How often should I check my blood pressure?</h4>
                                                    <p style="margin:0; font-size:15px;">If your BP is normal, once a
                                                        year during a check-up is fine. If you have been diagnosed with
                                                        hypertension, you should check it **at least once a week** at
                                                        home and keep a log for your doctor. BP can fluctuate, so a
                                                        one-time check isn't as useful as a long-term trend.</p>
                                                </div>
