                                                        What if I have to cancel?</h4>
                                                    <p style="margin:0; font-size:15px;">Consistency is vital for rehab.
                                                        However, we understand that life happens. We simply ask that you
                                                        give us at least 24 hours' notice if possible, so we can offer
                                                        that slot to another patient who might be in urgent pain.</p>
