                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can I still work if I'm on dialysis?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes!** Many dialysis patients
                                                        continue to work. We offer flexible scheduling, including
                                                        morning or evening sessions, to help you maintain your career
                                                        while getting the treatment you need.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        How many times a week do I need dialysis?</h4>
                                                    <p style="margin:0; font-size:15px;">For most patients with
                                                        permanent kidney failure, we recommend **3 sessions per week**,
                                                        with each session lasting 4 hours. This is the optimal amount to
                                                        keep toxin levels low and prevent complications like heart
                                                        strain.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Can a dialysis patient get a transplant?</h4>
                                                    <p style="margin:0; font-size:15px;">**Yes.** In fact, dialysis is
                                                        often referred to as "waiting room therapy" for a transplant. If
                                                        you are medically fit enough for surgery, our nephrologist will
                                                        help you begin the process of finding a donor and preparing for
                                                        a transplant.</p>
                                                </div>
                                                <div class="faq-item"
                                                    style="background:#f9f9f9; padding:15px 20px; border-radius:8px; margin-bottom:15px; border-left:4px solid #0c6574;">
                                                    <h4
                                                        style="margin:0 0 10px 0; font-size:18px; color:#333; font-weight:600;">
                                                        Is peritoneal dialysis (PD) better than hemodialysis?</h4>
                                                    <p style="margin:0; font-size:15px;">Neither is "better" for
                                                        everyone. PD is done at home using a tube in the abdomen and
                                                        offers more independence. Hemodialysis is done in a clinic using
                                                        a machine and is monitored by professionals. Our doctors at
                                                        LifeCare will help you choose the one that fits your lifestyle
                                                        best.</p>
                                                </div>
